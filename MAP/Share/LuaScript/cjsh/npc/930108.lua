function x930108_ProcAccept( varMap, varPlayer )
	idExt = x930108_var_SelectRecord[GetGUID(varMap, varPlayer)] 
	if idExt < 100 then
		WriteLog(1,format("x930108_ProcAccept,duihuanshenqi,illegal buttonid=%d, PlayerGUID=%d",idExt,GetGUID(varMap, varPlayer)))
		return
	end
	local selectid = floor(idExt/100)
	local selectext = mod(idExt,100)
	local havecount = GetItemCount(varMap, varPlayer, x930108_var_BonusItem[selectid]) --获取印记个数
	
	if havecount < x930108_var_ItemTable_Shop[selectid][GetZhiye(varMap,varPlayer)+1][selectext].num then
		Msg2Player(varMap, varPlayer, format("很抱歉，需要#R%d个#cffcf00@item_%d#cffcf00才能兑换",x930108_var_ItemTable_Shop[selectid][GetZhiye(varMap,varPlayer)+1][selectext].num,x930108_var_BonusItem[selectid] ), 8, 2)
		Msg2Player(varMap, varPlayer, format("很抱歉，需要#R%d个#cffcf00@item_%d#cffcf00才能兑换",x930108_var_ItemTable_Shop[selectid][GetZhiye(varMap,varPlayer)+1][selectext].num,x930108_var_BonusItem[selectid] ), 8, 3)
		return
	end
	
	if GetBagSpace(varMap, varPlayer) <= 0 then
		Msg2Player(varMap, varPlayer, "很抱歉，至少1个背包空位。", 0, 2)
		Msg2Player(varMap, varPlayer, "很抱歉，至少1个背包空位", 0, 3)
		return
	end
	
	if DelItem( varMap, varPlayer, x930108_var_BonusItem[selectid], x930108_var_ItemTable_Shop[selectid][GetZhiye(varMap,varPlayer)+1][selectext].num) ~= 1 then 
		return 0 
	end
		StartItemTask( varMap )
		ItemAppendBind( varMap, x930108_var_ItemTable_Shop[selectid][GetZhiye(varMap,varPlayer)+1][selectext].id, 1)
		local varRet = StopItemTask( varMap, varPlayer )
		if varRet > 0 then

			GamePlayScriptLog( varMap, varPlayer, 2509+ 3*(selectid-1)+ selectext)
			DeliverItemListSendToPlayer(varMap,varPlayer)
			Msg2Player( varMap, varPlayer, format( "兑换完成，获得#cffcf00@item_%d", x930108_var_ItemTable_Shop[selectid][GetZhiye(varMap,varPlayer)+1][selectext].id), 8, 2)
