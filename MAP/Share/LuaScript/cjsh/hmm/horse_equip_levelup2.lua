-- 获取马装ID
function x930200_GetEquipId(partIndex, typeIndex, levelIndex, breedIndex)
	if x930200_var_EquipId[partIndex] == nil then return -1 end
	if x930200_var_EquipId[partIndex][typeIndex] == nil then return -1 end
	if x930200_var_EquipId[partIndex][typeIndex][levelIndex] == nil then return -1 end
	return x930200_var_EquipId[partIndex][typeIndex][levelIndex][breedIndex].id or -1
end

-- 获取马装名称
function x930200_GetEquipName(partIndex, typeIndex, levelIndex, breedIndex)
	local levelName = x930200_var_LevelName[levelIndex] or "?"
	local breedName = x930200_var_BreedName[breedIndex] or "?"
	local typeName = x930200_var_TypeName[typeIndex] or "?"
	local partName = x930200_var_PartName[partIndex] or "?"
	return levelName .. "·" .. breedName .. typeName .. partName
end

-- 获取可用部位列表
function x930200_GetAvailableParts()
	local parts = {}
    local index = 1
	for partIndex, _ in x930200_var_EquipId do
		--table.insert(parts, ) 不支持
        parts[index] = {index = partIndex, name = x930200_var_PartName[partIndex]}
        index = index+1
	end
	return parts
end

function x930200_ProcEventEntry( varMap, varPlayer, varTalknpc, varScript, seleteId )

	if seleteId == -1 then
		-- 一级菜单：选择部位
		StartTalkTask(varMap)
		TalkAppendString(varMap, "#Y" .. x930200_var_QuestName)
		TalkAppendString(varMap, x930200_var_Questintro)
		TalkAppendString(varMap, "\n#W请选择要升级的马装部位：")
		local parts = x930200_GetAvailableParts()
		for _, part in parts do
			TalkAppendButton(varMap, x930200_var_FileId, "【" .. part.name .. "】", 3, part.index)
		end
		StopTalkTask()
		DeliverTalkMenu(varMap, varPlayer, varTalknpc)
		return
	end
	
	-- 二级菜单：选择类型  开山  追风  慧心
	if seleteId >= 1 and seleteId <= 20 then
		local partIndex = seleteId
		if x930200_var_EquipId[partIndex] == nil then
			Msg2Player(varMap, varPlayer, "该部位暂不支持升级", 8, 2)
			return
		end
		StartTalkTask(varMap)
		TalkAppendString(varMap, "#Y" .. x930200_var_QuestName .. " - " .. x930200_var_PartName[partIndex])
		TalkAppendString(varMap, "\n#W请选择马装类型：")
		TalkAppendString(varMap, "#G开山#W - 适合近攻职业（+力量）")
		TalkAppendString(varMap, "#G追风#W - 适合远攻职业（+敏捷）")
		TalkAppendString(varMap, "#G慧心#W - 适合魔攻职业（+智力）")
		for typeIndex = 1, 3 do
			local index = partIndex * 100 + typeIndex
			TalkAppendButton(varMap, x930200_var_FileId, "【" .. x930200_var_TypeName[typeIndex] .. "】", 3, index)
		end
		StopTalkTask()
		DeliverTalkMenu(varMap, varPlayer, varTalknpc)
		return
	end
	
	-- 三级菜单：选择等级  天地玄
	if seleteId >= 100 and seleteId < 10000 then
		local partIndex = floor(seleteId / 100)
		local typeIndex = mod(seleteId, 100)
		StartTalkTask(varMap)
		TalkAppendString(varMap, "#Y" .. x930200_var_QuestName)
		TalkAppendString(varMap, "\n#W请选择您当前马装的等级：")
		for levelIndex = 1, 5 do
			local index = partIndex * 10000 + typeIndex * 100 + levelIndex
			TalkAppendButton(varMap, x930200_var_FileId, "【" .. x930200_var_LevelName[levelIndex] .. "级】", 3, index)
		end
		StopTalkTask()
		DeliverTalkMenu(varMap, varPlayer, varTalknpc)
		return
	end
	
	-- 四级菜单：选择马种 三河，大宛
	if seleteId >= 10000 and seleteId < 1000000 then
		local partIndex = floor(seleteId / 10000)
		local typeIndex = mod(floor(seleteId / 100), 100)
		local levelIndex = mod(seleteId, 100)
		StartTalkTask(varMap)
		TalkAppendString(varMap, "#Y" .. x930200_var_QuestName)
		TalkAppendString(varMap, "\n#W请选择您当前马装的马种：")
		for breedIndex = 1, 5 do
			local index = partIndex * 1000000 + typeIndex * 10000 + levelIndex * 100 + breedIndex
			TalkAppendButton(varMap, x930200_var_FileId, "【" .. x930200_var_BreedName[breedIndex] .. "】", 3, index)
		end
		StopTalkTask()
		DeliverTalkMenu(varMap, varPlayer, varTalknpc)
		return
	end
	
	-- 五级菜单：展示升级选项
	if seleteId >= 1000000 and seleteId < 10000000 then
		local partIndex = floor(seleteId / 1000000)
		local typeIndex = mod(floor(seleteId / 10000), 100)
		local levelIndex = mod(floor(seleteId / 100), 100)
		local breedIndex = mod(seleteId, 100)
		
		local currentId = x930200_GetEquipId(partIndex, typeIndex, levelIndex, breedIndex)
		local currentName = x930200_GetEquipName(partIndex, typeIndex, levelIndex, breedIndex)
		
		if currentId == -1 then
			Msg2Player(varMap, varPlayer, "该马装配置不存在", 8, 2)
			return
		end
		
		StartTalkTask(varMap)
		TalkAppendString(varMap, "#Y" .. x930200_var_QuestName)
		TalkAppendString(varMap, "\n#W当前马装：#G" .. currentName)
		TalkAppendString(varMap, "#W当前持有：#R" .. GetItemCount(varMap, varPlayer, currentId) .. "#W个")
		TalkAppendString(varMap, "\n#W请选择升级方式：")
		
		-- 马种升级选项（如果不是大宛）
		if breedIndex < 5 then
			local nextId = x930200_GetEquipId(partIndex, typeIndex, levelIndex, breedIndex + 1)
			if nextId ~= -1 then
				local nextName = x930200_GetEquipName(partIndex, typeIndex, levelIndex, breedIndex + 1)
				local upgradeIndex = 10000000 + seleteId  -- 马种升级
				TalkAppendButton(varMap, x930200_var_FileId, "#G马种升级#W → " .. nextName, 3, upgradeIndex)
			end
		end
		
		-- 等级升阶选项（如果不是玄甲且是大宛）
		if levelIndex < 5 and breedIndex == 5 then
			local nextId = x930200_GetEquipId(partIndex, typeIndex, levelIndex + 1, 1)
			if nextId ~= -1 then
				local nextName = x930200_GetEquipName(partIndex, typeIndex, levelIndex + 1, 1)
				local levelUpIndex = 20000000 + seleteId  -- 等级升阶
				TalkAppendButton(varMap, x930200_var_FileId, "#Y等级升阶#W → " .. nextName, 3, levelUpIndex)
			end
		end
		
		if breedIndex == 5 and levelIndex == 5 then
			TalkAppendString(varMap, "\n#R您的马装已达最高级！")
		end
		
		StopTalkTask()
		DeliverTalkMenu(varMap, varPlayer, varTalknpc)
		return
	end
	
	-- 马种升级确认
	if seleteId >= 10000000 and seleteId < 20000000 then
		local baseIndex = seleteId - 10000000
		local partIndex = floor(baseIndex / 1000000)
		local typeIndex = mod(floor(baseIndex / 10000), 100)
		local levelIndex = mod(floor(baseIndex / 100), 100)
