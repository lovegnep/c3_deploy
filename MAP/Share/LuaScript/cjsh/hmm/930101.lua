-- 将列表元素打散
function x930101_randomtab(t)
    local len = getn(t)

    -- 拷贝一份，避免修改原 table
    local tmp = {}
    for i = 1, len do tmp[i] = t[i] end

    -- 洗牌
    for i = len, 2, -1 do
        local j = random(i)
        tmp[i], tmp[j] = tmp[j], tmp[i]
    end

    return tmp
end


