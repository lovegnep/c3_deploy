function x930189_getitemlevel( varMap, varPlayer, index)
    local item = x930189_var_table[index]

    --判断是否激活
    local curIndex = 0
    for vari,buff in item.buff do
        local haveImpact  = IsHaveSpecificImpact(varMap, varPlayer, buff.id)
        if haveImpact > 0 then
            curIndex = vari
        end
    end
    return curIndex
end
function x930189_gettotalbuff( varMap, varPlayer)
    local res = {}

    for vark, item in x930189_var_table do

        local index = 0
        for vari,buff in item.buff do
            local haveImpact  = IsHaveSpecificImpact(varMap, varPlayer, buff.id)
            if haveImpact > 0 then
                index = vari
            end
        end
        if index >0 then
            local btype = item.buff[index].bufftype
            local bvalue = item.buff[index].buffvalue
            if res[btype] ~= nil then
                res[btype] = res[btype] + bvalue
            else
                res[btype] = bvalue
            end
        end

    end

    local str = ""
    for btype, bvalue in res do
        str = str .. format("#G【%s】#W：+#G%d#W\n", btype, bvalue)
    end
    return str
end
