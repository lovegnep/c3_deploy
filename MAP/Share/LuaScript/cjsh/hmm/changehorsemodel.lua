function x930150_RandomSelect(varMap, varPlayer)
    local nan = 0
    if GetSex(varMap, varPlayer) > 0 then
        --男，要用偶数
        nan = 1
    end


	--	if 1==1 then return 551 end
	local Min = 1
	local Max = 1
    -- 先求总概率
    local totalProb = 0
    for i, iter in x930150_var_ModelList do
        if nan == 1 and mod(iter.buff[1],2) == 0 then
            --男
            local prob = iter.probability
            totalProb = totalProb + prob
        elseif nan ~= 1 and mod(iter.buff[1],2) ~= 0 then
            --女
            local prob = iter.probability
            totalProb = totalProb + prob
        end

    end

	local probability = random(1,totalProb)
	for varI,iter in x930150_var_ModelList do

        if nan == 1 and mod(iter.buff[1],2) == 0 then
            --男
            Max = Max + iter.probability
            if probability >= Min and probability < Max then
                return iter
            end
        elseif nan ~= 1 and mod(iter.buff[1],2) ~= 0 then
            --女
            Max = Max + iter.probability
            if probability >= Min and probability < Max then
                return iter
            end
        end


	end
	return -1
end

function x930150_GetRestCount(varMap, varPlayer)
	local guid = GetGUID(varMap, varPlayer)
	local hour, min, second = GetHourMinSec()
	if hour ~= x930150_var_CurHour then
		x930150_var_CountList = {}
		x930150_var_CurHour = hour
	end

	local count = x930150_var_CountList[guid]
	if count == nil then
		count = x930150_var_MaxCount
	end
	x930150_var_CountList[guid] = count - 1
	return count - 1
end

