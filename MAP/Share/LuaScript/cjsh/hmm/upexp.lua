function x930193_ProcEventEntry( varMap, varPlayer, varTalknpc ,varScript,seleteId)
    local level = GetLevel(varMap, varPlayer)
    if level < x930193_var_minlevel then
        Msg2Player(varMap, varPlayer,format("等级不足%d！",x930193_var_minlevel),8,3)
        Msg2Player(varMap, varPlayer,format("等级不足%d！",x930193_var_minlevel),8,2)
        return
    end
    if seleteId == -1 then

        --查询当前buffid
        local curIndex = 0
        for varI, item in x930193_var_ExcObjID do
            local haveImpact  = IsHaveSpecificImpact(varMap, varPlayer, item.buffID)
            if haveImpact > 0 then
                curIndex = varI
            end
        end

        local nextIndex = curIndex + 1
        if nextIndex > getn(x930193_var_ExcObjID) then
            StartTalkTask(varMap)
            TalkAppendString(varMap,"#Y您已经达到最大效果了")
            TalkAppendString(varMap,"\n")
            TalkAppendString(varMap,format("当前buff效果：#R%s#W",x930193_var_ExcObjID[curIndex].buffDesc))
            StopTalkTask()
            DeliverTalkInfo(varMap, varPlayer, varTalknpc, x930193_var_FileId, -1);
            return
        end

        local curJY = x930193_GetRefreshTimes(varMap,varPlayer)


        StartTalkTask(varMap)
        TalkAppendString(varMap,format("#Y您当前是#G%d#W级\n您当前经验#G%d#W，升级到下级需要经验#G%d#W！\n下一级效果是：#R%s#W\n\n",curIndex,curJY,x930193_var_ExcObjID[nextIndex].level,x930193_var_ExcObjID[nextIndex].buffDesc))
        TalkAppendString(varMap,"下10级需要的经验如下\n")
        local strs = x930193_getnext10desc(varMap, varPlayer,nextIndex)
        local sss = ""
        for varI, ss in strs do
            sss = sss .. ss
        end
        TalkAppendString(varMap,sss)

        StopTalkTask()
        DeliverTalkInfo(varMap, varPlayer, varTalknpc, x930193_var_FileId, -1);

    end

end

