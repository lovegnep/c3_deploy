function x930193_Upwaronleveladd(varMap, varPlayer, change)
    if change <= 0 then
        return
    end
    local warlevel = GetPlayerGameData(varMap, varPlayer, MD_GETBUFF_DAY1[1], MD_GETBUFF_DAY1[2], MD_GETBUFF_DAY1[3])

    local curLevel = 0
    local nextLevel = 0
    for vari, item in x930193_var_ExcObjID do
        if item.level >= warlevel then
            curLevel = vari
        end
        if item.level >= warlevel+change then
            nextLevel = vari
        end
    end

    --保存最新
    SetPlayerGameData(varMap, varPlayer, MD_GETBUFF_DAY1[1], MD_GETBUFF_DAY1[2], MD_GETBUFF_DAY1[3],warlevel+change)


    if curLevel == nextLevel then
        --没有任何变化
        return
    end


    if curLevel > 0 then
        local varRet = CancelSpecificImpact( varMap, varPlayer, x930193_var_ExcObjID[curLevel].buffID)
        if varRet <= 0 then
            Msg2Player(varMap, varPlayer,"删除效果"..x930193_var_ExcObjID[curLevel].buffID.."失败",8,3)
            Msg2Player(varMap, varPlayer,"删除效果"..x930193_var_ExcObjID[curLevel].buffID.."失败",8,2)
        else
            Msg2Player(varMap, varPlayer,"删除效果"..x930193_var_ExcObjID[curLevel].buffID.."成功",8,3)
            Msg2Player(varMap, varPlayer,"删除效果"..x930193_var_ExcObjID[curLevel].buffID.."成功",8,2)
        end
    end
    SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, x930193_var_ExcObjID[nextLevel].buffID, 0);
    Msg2Player(varMap, varPlayer,"添加效果"..x930193_var_ExcObjID[nextLevel].buffID.."成功",8,3)
