	for j, title in x930196_var_Title do
		if IsHaveTitle(varMap, varPlayer, title.varId) > 0 then
			TitleID = title.varId
			index = j
		end
	end
	local nextindex = index+1



	if TitleID > 0 then
		TalkAppendString(varMap, format("您现在的称号是%s", x930196_var_Title[index].varName))
		if nextindex <= getn(x930196_var_Title) then
