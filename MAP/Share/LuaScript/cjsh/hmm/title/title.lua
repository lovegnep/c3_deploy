	end
	StopTalkTask()
	DeliverTalkInfo(varMap, varPlayer, varTalknpc, x930196_var_FileId, -1)
	return
end




function x930196_ProcAcceptCheck(varMap, varPlayer, varTalknpc)

	local nTitleLevel = 0
	local varIndex = 0

	for varI, it in x930196_var_Title do
		if IsHaveTitle(varMap, varPlayer, it.varId) > 0 then
			nTitleLevel = it.varId
			varIndex = varI
		end
	end
	local varLevel = GetLevel(varMap, varPlayer)
	local nlevel= floor(varLevel/10)
	local mlevel =(nlevel+1)*10

	local nextindex = varIndex+1
	if nextindex >= getn(x930196_var_Title) then
		Msg2Player(varMap,varPlayer,"您已经升满了", 8, 3)
		Msg2Player(varMap,varPlayer,"您已经升满了", 8, 2)
		return
	end


	if varLevel < 40 then
		Msg2Player(varMap,varPlayer,"很抱歉，达到40级才可兑换或升级称号", 8, 3)
		Msg2Player(varMap,varPlayer,"很抱歉，达到40级才可兑换或升级称号", 8, 2)
		return
	end


	local varNum = x930196_var_Title[nextindex].neednum
	local needid = x930196_var_Title[nextindex].needid




	if GetItemCount(varMap, varPlayer, needid) < varNum then
		Msg2Player(varMap, varPlayer,format("你的#R@item_%d#W数量不足%d个，无法兑换", needid, varNum), 8, 3)
		Msg2Player(varMap, varPlayer,format("你的#R@item_%d#W数量不足%d个，无法兑换", needid, varNum), 8, 2)
		return
	end


	if 1~= DelItem(varMap, varPlayer, needid, varNum) then
		Msg2Player(varMap,varPlayer,"扣除道具失败", 8, 3)
		Msg2Player(varMap,varPlayer,"扣除道具失败", 8, 2)
		return
	end


	local nTitleID = 0
	if nTitleLevel > 0 then
		DeleteTitle(varMap,varPlayer, x930196_var_Title[varIndex].varId)
	end
	nTitleID = x930196_var_Title[nextindex].varId


	AwardTitle(varMap,varPlayer, nTitleID)

	WriteLog(1, format("x930196_ProcAcceptCheck 1 GetTitle OK: varMap(%d) GUID(%d) GetTitleID(%d)",
			varMap, GetGUID(varMap, varPlayer), nTitleID))
	
end





