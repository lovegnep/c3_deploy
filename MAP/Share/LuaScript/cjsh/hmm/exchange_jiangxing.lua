function x930173_ProcEventEntry( varMap, varPlayer, varTalknpc ,varScript,seleteId)

	if seleteId == -1 then
		--说明用户点击的是1级菜单，需要展示2级菜单
		StartTalkTask(varMap)
		TalkAppendString(varMap,"#Y"..format(x930173_var_QuestName,x930173_var_TargetName))
		TalkAppendString(varMap,x930173_var_Questintro)
		for varI, item in x930173_var_table  do
			TalkAppendButton( varMap, x930173_var_FileId, "【兑换】"..item.name, 3, item.index )
		end
		StopTalkTask()
		DeliverTalkMenu(varMap, varPlayer, varTalknpc)
	end

	if seleteId >= 1 and seleteId <= getn(x930173_var_table) then
		--说明用户点击的是2级菜单，需要展示3级菜单
		StartTalkTask(varMap)
		TalkAppendString(varMap,"#Y"..format(x930173_var_QuestName,x930173_var_table[seleteId].name))
		TalkAppendString(varMap,x930173_var_Questintro)
		for varI, item in x930173_var_table[seleteId].list  do
			local index = seleteId * 100 + varI
			TalkAppendButton( varMap, x930173_var_FileId, "【兑换】"..item.targetName, 3, index )
		end
		StopTalkTask()
		DeliverTalkMenu(varMap, varPlayer, varTalknpc)
	end

	if seleteId >= 101 then
		--说明用户点击的是3级菜单，需要展示最终兑换的道具
		x930173_var_SelectIndex[GetGUID(varMap, varPlayer)] = seleteId
		--对应石头的数组内的索引
		local sindex = mod(seleteId,100)
		local tindex = floor(seleteId/100) --类别

		local targetItem = x930173_var_table[tindex].list[sindex]
		local targetName = targetItem.targetName
		local targetID = targetItem.targetID

		StartTalkTask(varMap)
		TalkAppendString(varMap,"#Y"..format(x930173_var_QuestName,targetName))
		TalkAppendString(varMap,"需要的道具如下")
		for varI, item in targetItem.need  do
			local curCount = GetItemCount(varMap, varPlayer, item.id)
			TalkAppendString(varMap,format("#R%d#W个#G%s#W，您当前有#R%d#W个", item.num, item.name,curCount))
		end
		AddQuestItemBonus(varMap, targetID, 1)
		StopTalkTask()
		DeliverTalkInfo(varMap, varPlayer, varTalknpc, x930173_var_FileId, -1);



	end

end


