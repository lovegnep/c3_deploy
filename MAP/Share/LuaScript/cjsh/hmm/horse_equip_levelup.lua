function x930200_ProcAccept( varMap, varPlayer )
    local sid = x930200_var_SelectIndex[GetGUID(varMap, varPlayer)]
    if sid == -2 then
        --一键升级
        local cnt  = 100
        for i = 1, cnt do
            for eid, part in x930200_var_EquipId do
                for tid, row in part do
                    if GetItemCount(varMap, varPlayer, row.id) > 0 then
                        x930200_var_SelectIndex[GetGUID(varMap, varPlayer)] = eid*1000+tid
                        if x930200_DoUpgrade(varMap, varPlayer) == 0 then
                            return
                        end
                    end
                end
            end
        end

    else
        x930200_DoUpgrade(varMap, varPlayer)
    end


end

