		Msg2Player(varMap, varPlayer, "已经最大级别", 8, 2)
		Msg2Player(varMap, varPlayer, "已经最大级别", 8, 3)
		return 0
	end


    local mat = x930200_var_Material
    local cost = nil
    if mod(row.level+1, 5) == 0 then
        --升阶
        TalkAppendString(varMap, "\n#G阶级提升#W")
        cost = x930200_var_LevelUpCost

    else
        TalkAppendString(varMap, "\n#G等级提升#W")
        cost = x930200_var_UpgradeCost

    end

	
	-- 检查材料
	if GetItemCount(varMap, varPlayer, mat.yaotong.id) < cost.yaotong then
		Msg2Player(varMap, varPlayer, format("%s不足%d个", mat.yaotong.name, cost.yaotong), 8, 2)
		Msg2Player(varMap, varPlayer, format("%s不足%d个", mat.yaotong.name, cost.yaotong), 8, 3)
		return 0
	end
	if GetItemCount(varMap, varPlayer, mat.longlin.id) < cost.longlin then
		Msg2Player(varMap, varPlayer, format("%s不足%d个", mat.longlin.name, cost.longlin), 8, 2)
		Msg2Player(varMap, varPlayer, format("%s不足%d个", mat.longlin.name, cost.longlin), 8, 3)
		return 0
	end
	if GetItemCount(varMap, varPlayer, mat.fengyu.id) < cost.fengyu then
		Msg2Player(varMap, varPlayer, format("%s不足%d个", mat.fengyu.name, cost.fengyu), 8, 2)
		Msg2Player(varMap, varPlayer, format("%s不足%d个", mat.fengyu.name, cost.fengyu), 8, 3)
		return 0
	end
	
	-- 升阶还需要战神令
	if mod(row.level+1, 5) == 0 then
		if GetItemCount(varMap, varPlayer, mat.zhansheng.id) < cost.zhansheng then
			Msg2Player(varMap, varPlayer, format("%s不足%d个", mat.zhansheng.name, cost.zhansheng), 8, 2)
			Msg2Player(varMap, varPlayer, format("%s不足%d个", mat.zhansheng.name, cost.zhansheng), 8, 3)
			return 0
		end
	end
	
	-- 检查是否有当前马装
	if GetItemCount(varMap, varPlayer, row.id) < 1 then
		Msg2Player(varMap, varPlayer, format("您没有%s，无法升级！", row.name), 8, 2)
		Msg2Player(varMap, varPlayer, format("您没有%s，无法升级！", row.name), 8, 3)
		return 0
	end
	
	-- 扣除材料
	if DelItem(varMap, varPlayer, mat.yaotong.id, cost.yaotong) ~= 1 then
		Msg2Player(varMap, varPlayer, "扣除材料失败！", 8, 2)
		return 0
	end
	if DelItem(varMap, varPlayer, mat.longlin.id, cost.longlin) ~= 1 then
		Msg2Player(varMap, varPlayer, "扣除材料失败！", 8, 2)
		return 0
	end
	if DelItem(varMap, varPlayer, mat.fengyu.id, cost.fengyu) ~= 1 then
		Msg2Player(varMap, varPlayer, "扣除材料失败！", 8, 2)
		return 0
	end
	if mod(row.level+1, 5) == 0 then
		if DelItem(varMap, varPlayer, mat.zhansheng.id, cost.zhansheng) ~= 1 then
			Msg2Player(varMap, varPlayer, "扣除战神令失败！", 8, 2)
			return 0
		end
	end
	
	-- 扣除当前马装
	if DelItem(varMap, varPlayer, row.id, 1) ~= 1 then
		Msg2Player(varMap, varPlayer, "扣除当前马装失败！", 8, 2)
		return 0
	end
	
	-- 发放新马装
	StartItemTask(varMap)
	ItemAppendBind(varMap, nextrow.id, 1)
	local varRet = StopItemTask(varMap, varPlayer)
	if varRet > 0 then
		DeliverItemListSendToPlayer(varMap, varPlayer)
		if mod(row.level+1, 5) == 0 then
			Msg2Player(varMap, varPlayer, format("马种升级成功！获得%s", nextrow.name), 8, 3)
		else
			Msg2Player(varMap, varPlayer, format("等级升阶成功！获得%s", nextrow.name), 8, 3)
		end
		GamePlayScriptLog(varMap, varPlayer, 2671)
	else
		Msg2Player(varMap, varPlayer, "物品栏已满，无法获得物品！", 8, 2)
        return 0
	end
	
	return 1
end

