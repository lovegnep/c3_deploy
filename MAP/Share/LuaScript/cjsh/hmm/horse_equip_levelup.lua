function x930200_GetEquipId(partIndex, typeIndex, levelIndex, breedIndex)
	if x930200_var_EquipId[partIndex] == nil then return -1 end
	if x930200_var_EquipId[partIndex][typeIndex] == nil then return -1 end
	if x930200_var_EquipId[partIndex][typeIndex][levelIndex] == nil then return -1 end
	return x930200_var_EquipId[partIndex][typeIndex][levelIndex][breedIndex].id or -1
end

-- 获取马装名称
function x930200_GetEquipName(partIndex, typeIndex, levelIndex, breedIndex)
	local levelName = x930200_var_LevelName[levelIndex] or "?"
