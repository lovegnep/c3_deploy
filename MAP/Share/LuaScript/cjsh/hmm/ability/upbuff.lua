function x930209_Accept( varMap, varPlayer )


    --查询当前buffid
    local curIndex = 0
    for varI, item in x930209_var_ExcObjID do
        local haveImpact  = IsHaveSpecificImpact(varMap, varPlayer, item.buffID)
        if haveImpact > 0 then
            curIndex = varI
        end
    end

    local nextIndex = curIndex + 1
    if nextIndex > getn(x930209_var_ExcObjID) then
        Msg2Player(varMap, varPlayer,"您已经达到最大效果了！",8,3)
        Msg2Player(varMap, varPlayer,"您已经达到最大效果了！",8,2)
        return
    end

    --检查道具
    for k, item in x930209_var_ExcObjID[nextIndex] do
        if GetItemCount( varMap, varPlayer, item.id ) < item.num then
            Msg2Player(varMap, varPlayer,format("道具@item_%d数量不足%d个", item.id, item.num),8,3)
            Msg2Player(varMap, varPlayer,format("道具@item_%d数量不足%d个", item.id, item.num),8,2)
            return
        end
    end

    --扣除道具
    for k, item in x930209_var_ExcObjID[nextIndex] do
        if DelItem(varMap, varPlayer, item.id, item.num) ~= 1 then
            Msg2Player(varMap, varPlayer,format("道具@item_%d数量不足%d个", item.id, item.num),8,3)
            Msg2Player(varMap, varPlayer,format("道具@item_%d数量不足%d个", item.id, item.num),8,2)
            return
        end
    end



    if curIndex > 0 then
        local varRet = CancelSpecificImpact( varMap, varPlayer, x930209_var_ExcObjID[curIndex].buffID)
        if varRet <= 0 then
            Msg2Player(varMap, varPlayer,"删除效果"..x930209_var_ExcObjID[curIndex].buffID.."失败",8,3)
            Msg2Player(varMap, varPlayer,"删除效果"..x930209_var_ExcObjID[curIndex].buffID.."失败",8,2)
            return
        else
            Msg2Player(varMap, varPlayer,"删除效果"..x930209_var_ExcObjID[curIndex].buffID.."成功",8,3)
            Msg2Player(varMap, varPlayer,"删除效果"..x930209_var_ExcObjID[curIndex].buffID.."成功",8,2)
        end
    end
    SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, x930209_var_ExcObjID[nextIndex].buffID, 0);
    Msg2Player(varMap, varPlayer,"添加效果"..x930209_var_ExcObjID[nextIndex].buffID.."成功",8,3)
    Msg2Player(varMap, varPlayer,"添加效果"..x930209_var_ExcObjID[nextIndex].buffID.."成功",8,2)

