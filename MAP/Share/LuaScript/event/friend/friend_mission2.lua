function x300782_IsFriendHere(varMap, varPlayer)
	local varTeamSize = GetTeamSize(varMap, varPlayer)
	if (varTeamSize ~= 2) then
		local friendName = GetFriendName(varMap, varPlayer, x300782_GetFriendGUID(varMap, varPlayer))
		if "" ~= friendName then
			return "你应该与" .. friendName .. "两人组队来进行此任务。"
		end
		return "请你和你的朋友两人组队来进行此任务。";
	end

	local nearTeamSize = GetNearTeamCount(varMap, varPlayer)

	if varTeamSize ~= nearTeamSize then
		return "你和你的朋友必须都在附近。"
	end

	local varTalknpc = GetNearTeamMember(varMap, varPlayer, 0)
	
	if varTalknpc == varPlayer then
		varTalknpc = GetNearTeamMember(varMap, varPlayer, 1)
	end
	
	if IsInDist(varMap, varPlayer, varTalknpc, 30) ~= 1 then
		return "你和你的朋友必须都在附近。"
	end

	if IsFriend(varMap, varPlayer, varTalknpc) ~= 1 then
		return "请互相添加对方为好友再来进行此任务。";
	end
	
	if IsFriend(varMap, varTalknpc, varPlayer ) ~= 1 then
		return "请互相添加对方为好友再来进行此任务。";
	end
	
	if x300782_GetFriendGUID(varMap, varPlayer) ~= GetGUID(varMap, varTalknpc) then
		local friendName = GetFriendName(varMap, varPlayer, x300782_GetFriendGUID(varMap, varPlayer))
		if "" ~= friendName then
			return "你应该与" .. friendName .. "两人组队来进行此任务。"
		end
	end
	
	if x300782_GetFriendGUID(varMap, varTalknpc) ~= GetGUID(varMap, varPlayer) then
		local friendName = GetFriendName(varMap, varTalknpc, x300782_GetFriendGUID(varMap, varTalknpc))
		if "" ~= friendName then
			return "你的队友应该与" .. friendName .. "两人组队来进行此任务。"
		end
	end
	
	local selfHaveQuest = x300782_HaveWhichSubQuest(varMap, varPlayer)
	local friendHaveQuest = x300782_HaveWhichSubQuest(varMap, varTalknpc)
	
	if selfHaveQuest ~= friendHaveQuest then
		return "你的好友已经放弃任务，请你删除任务。"
	end
	
	if IsPlayerStateNormal(varMap, varPlayer) <= 0 or GetHp(varMap, varPlayer) <= 0 then
		return "你当前状态无法进行此任务。"
	end
	
	if IsPlayerStateNormal(varMap, varTalknpc) <= 0 or GetHp(varMap, varTalknpc) <= 0 then
		return "你好友的当前状态无法进行此任务。"
	end
	
	return nil
		
end


