  end
  
  local questType,varIndex = x300782_GetQuestInfo(quest)
  if questType ~= x300782_var_QuestType.KillMonster then
		return -1
	end
	
	if IsQuestHaveDoneNM( varMap, varPlayer, quest ) > 0 then 
