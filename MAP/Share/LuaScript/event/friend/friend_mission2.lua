function x300782_PositionUseItem( varMap, varPlayer, varBagIdx, varImpact )

	local varQuest = x300782_HaveWhichSubQuest(varMap, varPlayer)
	if varQuest == -1 then
		return 0
	end
	local questType,varIndex = x300782_GetQuestInfo(varQuest)
	if questType ~= x300782_var_QuestType.UseItem then
		return 0
	end

	local varQuestIdx = GetQuestIndexByID( varMap, varPlayer, varQuest )
	if GetQuestParam( varMap, varPlayer, varQuestIdx, 1 ) == 1 then
		return 0
	end

	local isScene = 0
	for varI, scene in x300782_var_UseItem[varIndex].scene do
		if scene == varMap then
			isScene = 1
		end
	end

	if isScene ~= 1 then
		return 0
	end

	local scene = x300782_var_UseItem[varIndex].scene[GetCurCountry(varMap, varPlayer)+1]
	local posx = x300782_var_UseItem[varIndex].posX
	local posz = x300782_var_UseItem[varIndex].posZ
	local radii = 10

	if posx > 0 then 
		local PlayerPosX,PlayerPosZ = GetWorldPos( varMap, varPlayer )

		local distanceRet = radii * radii - (posx - PlayerPosX) * (posx - PlayerPosX) - (posz - PlayerPosZ) * (posz - PlayerPosZ)
		if distanceRet <= 0 then
			local varText = format("很抱歉，这里不能使用该物品，请到@sceneid_%d(%d,%d)处使用!", scene, posx, posz )
			StartTalkTask(varMap)
			TalkAppendString(varMap, varText)
			StopTalkTask(varMap)
			DeliverTalkTips(varMap, varPlayer)
			return 0
		end
	end

	local retCode = x300782_IsFriendHere(varMap, varPlayer)
	if retCode ~= nil then
			StartTalkTask(varMap)
			TalkAppendString(varMap, retCode)
			DeliverTalkTips(varMap, varPlayer)
			StopTalkTask(varMap)
			return
	end

	StartTalkTask(varMap)
	TalkAppendString(varMap, "象征友谊的烟花绽放在天空中")
	StopTalkTask(varMap)
	DeliverTalkTips(varMap, varPlayer)

	
	if EraseItem(varMap, varPlayer, varBagIdx) ~= 1 then return 0 end

	
	if varImpact ~= -1 then
		SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, varImpact, 0);
	end
	SetQuestByIndex( varMap, varPlayer, varQuestIdx, 0, 1 )
	SetQuestByIndex( varMap, varPlayer, varQuestIdx, 7, 1 )

	
	return 1

