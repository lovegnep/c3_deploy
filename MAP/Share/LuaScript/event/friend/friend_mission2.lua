function x300782_RandomQuest(varMap, varPlayer, varTalknpc)
	local randomTable = {}
	local myLevel = GetLevel(varMap, varPlayer)
	local targetLevel = GetLevel(varMap, varTalknpc)
	local average = myLevel
	if myLevel > targetLevel then
		average = targetLevel
	end
	local beginIndex
	local endIndex
	if average >= 40 then
		beginIndex = 1
		endIndex = 15
	elseif average >= 35 then
		beginIndex = 13
		endIndex = beginIndex + 2
	elseif average >= 30 then
		beginIndex = 10
		endIndex = beginIndex + 2
	elseif average >= 25 then
		beginIndex = 7
		endIndex = beginIndex + 2
	elseif average >= 20 then
		beginIndex = 1
		endIndex = 6
	end
	
	local varIndex = 1
	for varI, quest in x300782_var_LootItem do
		randomTable[varIndex] = quest
		varIndex = varIndex + 1
	end
	for varI, quest in x300782_var_UseItem do
		randomTable[varIndex] = quest.varQuest
		varIndex = varIndex + 1
	end
	for varI, quest in x300782_var_PostMail do
		randomTable[varIndex] = quest.varQuest
		varIndex = varIndex + 1
	end
