function x300903_ProcQuestItemChanged( varMap, varPlayer, varItemData, varQuest, nChangeType )
	local nCheckItemId = x300903_CheckWhichItem(varMap, varPlayer)
	if nCheckItemId ~= varItemData then
		return
	end

    if varQuest == nil or varQuest < 0 then
        return
    end
    
    if IsHaveQuestNM(varMap,varPlayer,varQuest) <= 0 then
        return
    end
	
  	local NeedNum, ObjIndex = GetQuestNeedItemNumNM( varMap, varPlayer, varQuest, varItemData )
	local varQuestIdx = GetQuestIndexByID( varMap, varPlayer, varQuest )

	if nChangeType ==1 then
		
		local itemNum = GetItemCount( varMap, varPlayer, varItemData )
		if itemNum < NeedNum then
			SetQuestByIndex( varMap, varPlayer, varQuestIdx, 0, itemNum )
			SetQuestByIndex( varMap, varPlayer, varQuestIdx, 7, 0 )
			x300903_QuestLogRefresh( varMap, varPlayer, varQuest );
		end
		return
	end
	
	if NeedNum <= 0 then
		return
	end
	

	local Num = GetItemCount( varMap, varPlayer, varItemData )
	if Num < NeedNum and Num > 0 then 
		StartTalkTask(varMap)
		local varText = format("已得到@itemid_%d: %d/%d", varItemData, Num, NeedNum )
		if varText == nil then
			varText = "";
		end
		TalkAppendString( varMap, varText )
		StopTalkTask( varMap )
		DeliverTalkTips( varMap, varPlayer )
		SetQuestByIndex( varMap, varPlayer, varQuestIdx, 0, Num )
		x300903_QuestLogRefresh( varMap, varPlayer, varQuest );
	elseif Num >= NeedNum then
		
		StartTalkTask(varMap)
		local varText = format( "已得到足够的@itemid_%d: %d/%d", varItemData, Num, NeedNum )
		if varText == nil then
			varText = "";
		end
		TalkAppendString( varMap, varText )
		StopTalkTask( varMap )
		DeliverTalkTips( varMap, varPlayer )
		SetQuestByIndex( varMap, varPlayer, varQuestIdx, 7, 1 )
		SetQuestByIndex( varMap, varPlayer, varQuestIdx, 0, Num )
		x300903_QuestLogRefresh( varMap, varPlayer, varQuest );
	end

end

function x300903_QuestLogRefresh( varMap, varPlayer, varQuest)


	local varHaveQuest = IsHaveQuestNM(varMap, varPlayer,varQuest );
	if varHaveQuest <= 0 then
		return
	end
	
	StartTalkTask(varMap)	
	local varLevel = GetLevel(varMap, varPlayer)
	local varExpBonus = ceil ( (100 * 3 * varLevel * 20*1*1 ) )
	varExpBonus =floor(varExpBonus+0.5)
	if varExpBonus > 0 then
	AddQuestExpBonus(varMap, varExpBonus);		
	end
	if x300903_var_BonusMoney8 > 0 then
	AddQuestMoneyBonus8(varMap, x300903_var_BonusMoney8 )	
	end
	
	
	

	local nCheckItemId = x300903_CheckWhichItem(varMap, varPlayer)
	if 0 == nCheckItemId then
		return
	end
	local itemNum = GetItemCount( varMap, varPlayer, nCheckItemId )
	local NeedNum, ObjIndex = GetQuestNeedItemNumNM( varMap, varPlayer, varQuest, nCheckItemId )	
	if itemNum >= NeedNum then
		itemNum = NeedNum
	end
	local text = format("(%d/%d)",itemNum,NeedNum)
	local varQuestDesc = x300903_GetQuestDesc(varMap, varPlayer)
	if nil == varQuestDesc then
		return
	end
	AddQuestLogCustomText( varMap,
							"",						
							varQuestDesc.varQuestName,        
							varQuestDesc.varQuestTarget..text,		
							varQuestDesc.NpcName,			
							varQuestDesc.Questruse,               
							varQuestDesc.QuestInfo,	
							"每张领地地图都有古老的图腾，请根据自身等级进行选择。随着你所在帮会等级的提高，你获得的奖励也会相应增加。"					
							)
	StopTalkTask(varMap)
	DeliverTalkRefreshQuest(varMap, varPlayer, varQuest);
end

function x300903_ProcQuestLogRefresh( varMap, varPlayer, varQuest)

	x300903_QuestLogRefresh( varMap, varPlayer, varQuest );
end




