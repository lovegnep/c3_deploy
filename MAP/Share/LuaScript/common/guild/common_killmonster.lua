	for varI,itm in x300619_var_SubQuestId[varCountry+1] do 
		local varHaveQuest = IsHaveQuestNM(varMap, varPlayer,itm.varQuest );
		if varHaveQuest > 0 then
		
			StartTalkTask(varMap)	
			local varLevel = GetLevel(varMap, varPlayer)
			
			local varExpBonus = ceil ( (100 * 3 * varLevel * 15*1*1 ) )
			varExpBonus =floor(varExpBonus+0.5)
			if varExpBonus > 0 then
			AddQuestExpBonus(varMap, varExpBonus);		
			end
			if x300619_var_BonusMoney8 > 0 then
			AddQuestMoneyBonus8(varMap, x300619_var_BonusMoney8 )	
			end
			if x300619_var_GuildExpBonus>0 then
			AddQuestGuildExpBonus(varMap,x300619_var_GuildExpBonus)
			end

			local varQuestIdx = GetQuestIndexByID(varMap, varPlayer, itm.varQuest);
			local varNum = GetQuestParam(varMap, varPlayer,varQuestIdx,0)
			local text = format("(%d/%d)",varNum,1)
		   AddQuestLogCustomText( varMap,
									"",						
									itm.varQuestName,        
									itm.varQuestTarget..text,		
									itm.NPCName,			
									itm.Questruse,               
									itm.QuestInfo,	
									"每张领地地图都有一个魔君的爪牙，请根据自身等级进行选择。随着你所在帮会等级的提高，你获得的奖励也会相应增加。"					
									)
			StopTalkTask(varMap)
			DeliverTalkRefreshQuest(varMap, varPlayer, itm.varQuest);
		end
		break
	end
end

