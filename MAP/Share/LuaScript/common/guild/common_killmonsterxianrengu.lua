function x300826_ProcDie( varMap,varObj,varKiller )

	if varKiller == -1 then
		
		return 
	end

	if IsObjValid(varMap,varKiller)  == 0 then
		
		return
	end

	local ObjType = GetObjType(varMap,varKiller)

	if ObjType == 3 then		
		ObjType = 1
		varKiller = GetOwnerID(varMap, varKiller)
	end

	if ObjType ~= 1 then
		return
	end

	if IsPlayerStateNormal( varMap,varKiller ) ~= 1 then
		return
	end

	local varQuest = -1
	local varObjData = -1
	local varCountry = GetCurCountry( varMap, varKiller )
	
 	for it,itm in x300826_var_SubQuestId[varCountry+1] do
		if IsHaveQuestNM( varMap, varKiller, itm.varQuest ) > 0 then	
			varQuest = itm.varQuest
			varObjData = itm.varObjData
			break
		end
	end

	local varText = "";
	if varQuest ~= -1 and varObjData == GetMonsterDataID(varMap,varObj) then

		
		local varQuestIdx = GetQuestIndexByID( varMap, varKiller, varQuest )
		local KilledNum = GetQuestParam( varMap, varKiller, varQuestIdx, 0 )

		if KilledNum < x300826_var_NeedKilledNum then	
			SetQuestByIndex( varMap, varKiller, varQuestIdx, 0, KilledNum+1 )
			x300826_QuestLogRefresh( varMap, varKiller, varQuest)

			StartTalkTask(varMap)
			
			if GetName(varMap,varObj) == "" then
				varText = format( "已经杀死怪物: %d/%d", KilledNum+1, x300826_var_NeedKilledNum )
			else
				varText = format( "已经杀死%s: %d/%d", GetName(varMap,varObj), KilledNum+1, x300826_var_NeedKilledNum )
			end

			TalkAppendString( varMap, varText )
			StopTalkTask( varMap )
			DeliverTalkTips( varMap, varKiller )

			if KilledNum == x300826_var_NeedKilledNum - 1 then
				
				SetQuestByIndex(varMap, varKiller, varQuestIdx, 7, 1);
				local varStr = format("%s完成任务",GetName(varMap,varKiller) )
			end
		end

	end

	if GetGuildID(varMap, varKiller) == -1 or HasTeam(varMap, varKiller) < 1 then		
		return 
	end

	
	local varNum =  GetNearTeamCount(varMap, varKiller)
	for	varI=0,varNum-1 do

		local member = GetNearTeamMember(varMap, varKiller,varI)
		if member ~= -1 and member ~= varKiller and GetGuildID(varMap, member) == GetGuildID(varMap, varKiller) and IsPlayerStateNormal( varMap,member ) == 1 then

			local varQuest = -1
			local varObjData = -1

			for n,itm in x300826_var_SubQuestId[varCountry+1] do
				if IsHaveQuestNM( varMap, member, itm.varQuest ) > 0 then	
					varQuest = itm.varQuest
					varObjData = itm.varObjData
					break
				end
			end
			if GetHp(varMap, member) > 0 then
				if varQuest ~= -1  and varObjData == GetMonsterDataID(varMap,varObj)  then
					local MemberMisIndex = GetQuestIndexByID( varMap, member, varQuest )
					local MemberKilledNum = GetQuestParam( varMap, member, MemberMisIndex, 0 )

					if MemberKilledNum < x300826_var_NeedKilledNum then	
				
						StartTalkTask(varMap)
						if GetName(varMap,varObj) == "" then
							varText = format( "已经杀死怪物: %d/%d", MemberKilledNum+1, x300826_var_NeedKilledNum )
						else
							varText = format( "已经杀死%s: %d/%d", GetName(varMap,varObj), MemberKilledNum+1, x300826_var_NeedKilledNum )
						end
						TalkAppendString( varMap, varText )
						StopTalkTask( varMap )
						DeliverTalkTips( varMap, member )

						SetQuestByIndex( varMap, member, MemberMisIndex, 0, MemberKilledNum+1 )
