function x560888_GetShopIdByShopIndex(varMap, varPlayer, shopindex)

	
	if shopindex >= 1 and shopindex <= x560888_var_StaticShopNum then
		return x560888_var_StaticShopList[shopindex]	
	end

	
	if shopindex == 0 then	
		return x560888_GetDynamicShopId(varMap, varPlayer)
	end
	
	
	if varIndex == x560888_var_TodayShopIndex then	
		
		local nWeek = GetWeek()
		if nWeek >=0 and nWeek < 7 then
			return x560888_var_TodayShopList[nWeek+1]		
		end	
	end
	
	return -1
end


function x560888_GetShopIndexByShopId(varMap, varPlayer, shopid)

	
	if (shopid >= 401) and (shopid <= 543) then
		return (shopid - 401 +1)
	end
	
	
	if (shopid >= 544 and shopid <= 603) or (shopid >= 1544 and shopid <= 1603) or
		 (shopid >= 604 and shopid <= 663) or (shopid >= 1604 and shopid <= 1663) or
		 (shopid >= 664 and shopid <= 723) or (shopid >= 1664 and shopid <= 1723) then	
		if shopid == x560888_GetDynamicShopId (varMap, varPlayer) then
			return 0
		end
				
	end
	
	
	if (shopid >= 724) and (shopid <= 730) then
	
		
		local nWeek = GetWeek()
		if nWeek >=0 and nWeek < 7 then
			if shopid == x560888_var_TodayShopList[nWeek+1]	then
				return x560888_var_TodayShopIndex;
			end
		end				
	end
	
	return -1;
	
end

function x560888_GetDynamicShopId(varMap, varPlayer)

	local	zhiye = GetZhiye(varMap, varPlayer)

	if zhiye < 0 or zhiye >= 12 then
		return -1
	end
	
	for varI,varLevel in x560888_var_DynamicLevelList[zhiye+1] do			
		if GetLevel(varMap,varPlayer) < varLevel then	
			local shopId = x560888_var_DynamicShopList3[zhiye+1][varI]
			if x560888_IsValidShop(varMap, varPlayer,shopId) == 1 then					
				return shopId
			end

			shopId = x560888_var_DynamicShopList2[zhiye+1][varI]
