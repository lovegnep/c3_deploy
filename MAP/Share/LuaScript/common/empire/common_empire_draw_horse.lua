function x310275_ProcEventEntry( varMap , varPlayer , varTalknpc , varState , varIndex )

	StartTalkTask(varMap)
	local Readme_1 ="#Y【帝国】领取皇帝坐骑#W#r"
	local Readme_2 ="\t我只为本国#G皇帝#W和#G皇后#W服务，他们可以在我这里领取#G皇帝专有坐骑#W。#r" 
	local Readme_3 ="\t皇帝坐骑，会根据当前国家的皇帝连任次数，自动领取对应的坐骑。连任次数越多，领取的坐骑能力越强。\n\t当然，一旦连任皇帝失败，下一次连任次数将从头计数。#r"
	local Readme_4 ="\t如果#G误将皇帝坐骑放生#W，没有必要担心，皇帝（皇后）可以随时在我这里#G重新领取#W。#r"
	local Readme_5 = ""
	local curEmpirePos = GetEmpirePosition( varMap, varPlayer )
	if curEmpirePos == EMPIRE_POSITION_EMPEROR or curEmpirePos == EMPIRE_POSITION_EMPRESS then
	   local CurHorseInfo, nLianRenNum = x310275_GetCurHorseInfo( varMap, varPlayer )
	   local strDesc = ""
	   if CurHorseInfo ~= nil then
	        strDesc = CurHorseInfo.desc
	   end 
	   Readme_5 = format("\t您当前的连任次数为#G%d#W次，可以领取#G%s#W。#r", nLianRenNum, strDesc )
	end	 
    
    local varReadme = Readme_1..Readme_2.. Readme_3..Readme_4..Readme_5
	TalkAppendString(varMap,varReadme)
	StopTalkTask(varMap)	
	DeliverTalkInfo(varMap,varPlayer,varTalknpc,x310275_var_FileId,-1)
		
end




