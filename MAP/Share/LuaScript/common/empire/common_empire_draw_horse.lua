function x310275_ProcAccept( varMap, varPlayer )
	
	local curEmpirePos = GetEmpirePosition( varMap, varPlayer)
	
	if curEmpirePos ~= EMPIRE_POSITION_EMPEROR and curEmpirePos ~= EMPIRE_POSITION_EMPRESS then
		StartTalkTask(varMap)
		TalkAppendString(varMap, "很抱歉，您不是皇帝/皇后，无法领取皇帝坐骑。")
		DeliverTalkTips(varMap, varPlayer)
		StopTalkTask(varMap)
		Msg2Player(varMap,varPlayer,"很抱歉，您不是皇帝/皇后，无法领取皇帝坐骑。",8,2)
		return
	end 

    local curCanGetHorseInfo = x310275_GetCurHorseInfo( varMap, varPlayer )
    if curCanGetHorseInfo == nil then
        StartTalkTask(varMap)
		TalkAppendString(varMap, "很抱歉，您的等级不足，无法领取皇帝坐骑。")
		DeliverTalkTips(varMap, varPlayer)
		StopTalkTask(varMap)
		Msg2Player(varMap,varPlayer,"很抱歉，您的等级不足，无法领取皇帝坐骑。",8,2)
		return
    end
    
    if IsHaveHorse( varMap, varPlayer, curCanGetHorseInfo.horseId ) > 0 then
        
        StartTalkTask(varMap)
		TalkAppendString(varMap, "很抱歉，您已经有了皇帝坐骑，不能反复领取。")
		DeliverTalkTips(varMap, varPlayer)
		StopTalkTask(varMap)
		Msg2Player(varMap,varPlayer,"很抱歉，您已经有了皇帝坐骑，不能反复领取。",8,2)
		return
           
    end
    
    
    --删除玩家身上所有皇帝坐骑
    for i, item in x310275_var_EHorseTable do
        for j, item2 in item do
            if IsHaveHorse( varMap, varPlayer, item2.horseId ) > 0 then
    			FreeHorseByType(varMap, varPlayer, item2.horseId )
        	end
        end
    end 
    
    --给坐骑
    if CreateHorseToPlayer( varMap, varPlayer, curCanGetHorseInfo.horseId, -1 ) < 0 then
		StartTalkTask(varMap)
		TalkAppendString(varMap, "很抱歉，您的坐骑栏已满，无法领取。")
		DeliverTalkTips(varMap, varPlayer)
		StopTalkTask(varMap)
		Msg2Player(varMap,varPlayer,"很抱歉，您的坐骑栏已满，无法领取。",8,2)
		return
    end
    
    
    --加经验
    local varCount = GetPetCount( varMap, varPlayer )
    local idx = 0
    for varI = 0, varCount - 1 do
        if GetHorseDataID( varMap, varPlayer, varI) == curCanGetHorseInfo.horseId then
            idx = varI
            break
        end
    end
    local varLevel = GetLevel(varMap, varPlayer)
    for varI = 1, varLevel - 1 do
        if varI == varLevel - 1 then
            AddHorseExp( varMap, varPlayer, idx, x310275_var_LevelupTable[ varI], 1)
        else
            AddHorseExp( varMap, varPlayer, idx, x310275_var_LevelupTable[ varI], 0)
        end
    end
    
    Msg2Player( varMap,varPlayer, "恭喜您，您顺利的获得了皇帝坐骑。", 8, 2 )

end


function x310275_GetCurHorseInfo( varMap, varPlayer )
    
    local country = GetCurCountry( varMap, varPlayer )
    local nLianRenNum = GetCountryParamByBit( country,  CD_KAISER_BATTLE_INFO, 10, 7 )
    local nTableIndex = x310275_var_EHorseTableMaxCount - 1
    if nTableIndex > nLianRenNum then
        nTableIndex = nLianRenNum
    end

    local varLevel = GetLevel(varMap, varPlayer)
    local nTableIndex2 = 0
    for i, item in x310275_var_EHorseTable[ nTableIndex ] do
        if varLevel >= item.minLevel and varLevel <= item.maxLevel then
            nTableIndex2 = i 
            break
        end 
    end
    
    if nTableIndex2 == 0 then
        return nil,nLianRenNum
    else
        return x310275_var_EHorseTable[nTableIndex][nTableIndex2],nLianRenNum
    end
end


