function x700223_EnterTick(varMap,nowTickCount)

	local nBoss1 = GetFubenData_Param(varMap, x700223_CSP_BOSS1_ID )
	
	if nBoss1>=0 then
		local humancount = GetFuben_PlayerCount(varMap);
		if humancount>0 then
			local nCurrentHp = GetHp( varMap, nBoss1 )
			local nFullHp = GetFubenData_Param(varMap, x700223_CSP_BOSS1_FULL_HP )
			
			local bCreatedSubMonster = GetFubenData_Param(varMap, x700223_CSP_BOSS1_LOADMONSTER )
			
			if nFullHp>0 then
				if nCurrentHp/nFullHp <0.9 then
					if bCreatedSubMonster<=0 then
						x700223_CreateBoss2(varMap)
					end
				end
			end
		end
	end

	local nBoss3Flag = GetFubenData_Param(varMap, x700223_CSP_BOSS3_FLAG) ;
	if nBoss3Flag>0 then
		if mod(nowTickCount,6) ==0 then  
			x700223_CreateChildDragon(varMap, x700223_var_ProceTime_Child_Count)
		end
	end

	local varCount = GetFubenData_Param(varMap, x700223_CSP_PATROL_MONSTER_COUNT)
	local nCount_dup = varCount;
	local nFullHp = GetFubenData_Param(varMap, x700223_CSP_PATROL_MONSTER_FULL_HP)
	for varI=0, varCount-1 do
		local nObjId = GetFubenData_Param(varMap, x700223_CSP_PATROL_MONSTER_START+varI)
		if nObjId>=0 then
			local nCurrentHp = GetHp( varMap, nObjId )
			if nCurrentHp/nFullHp<=0.2 then
				SetFubenData_Param(varMap, x700223_CSP_PATROL_MONSTER_START+varI,-1)
				
				local varX,z =GetWorldPos(varMap,nObjId)
				DeleteMonster( varMap,nObjId )
				x700223_CreateChangeMonster(varMap,varX,z)
				
			end
		end
	end

	for varI=0, varCount-1 do
		local nObjId = GetFubenData_Param(varMap, x700223_CSP_PATROL_MONSTER_START+varI)
		
		if nObjId ==-1 then
		
			x700223_ArrayDeleteByIndex(varMap,x700223_CSP_PATROL_MONSTER_START,varCount,varI)
			nCount_dup = nCount_dup -1;
			
		end
	end

	SetFubenData_Param(varMap, x700223_CSP_PATROL_MONSTER_COUNT,nCount_dup )

	
end

function x700223_ArrayDeleteByIndex(varMap,arrayId,arrayLen, varIndex)
	if arrayLen< varIndex+1 then
		return
	end

	if arrayLen<=0 or varIndex<0 then
		return
	end

	for varI=varIndex, arrayLen-2 do
		local nNextValue = GetFubenData_Param(varMap, arrayId+varI+1)
		SetFubenData_Param(varMap, arrayId+varI,nNextValue)
	end
	
end


function x700223_CreateBoss(varMap)


	local copyscenelevel = GetFubenData_Param(varMap, x700223_CSP_FUBENLEVEL ) ;

	
	for varI, item in x700223_var_BossCreate do

        if item.levelmin <= copyscenelevel and item.levelmax >= copyscenelevel then
		    local varRet = 0
			if item.title~="" then
				varRet = CreateMonster(varMap, item.type, item.varX, item.z, item.ai, item.aiscript, x700223_var_FileId, -1, 21,-1,item.facedir,  "",item.title)
			else
				varRet = CreateMonster(varMap, item.type, item.varX, item.z, item.ai, item.aiscript, x700223_var_FileId, -1, 21,-1,item.facedir)
				
			end
			if item.varFlag ==0 then
				SetFubenData_Param(varMap, x700223_CSP_BOSS1_ID, varRet ) ;
				local nFullHp = GetHp( varMap, varRet )
				SetFubenData_Param(varMap, x700223_CSP_BOSS1_FULL_HP, nFullHp )
				SetFubenData_Param(varMap, x700223_CSP_BOSS1_LOADMONSTER, 0 )
			elseif item.varFlag ==1 then
				SetFubenData_Param(varMap, x700223_CSP_BOSS2_ID, varRet ) ;
			end
        end
	end

end



function x700223_CreateBoss2(varMap)

