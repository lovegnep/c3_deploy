function x700079_ProcEnumEvent(varMap, varPlayer, varTalknpc, varQuest)

	if GetGameOpenById(1022)<=0 then
		
		return 0;
	end
	
	if IsPlayerStateNormal(varMap,varPlayer ) <= 0 then
		return
	end


	local varIndex, strName = x700079_GetFubenName(varMap, varPlayer)


	if varIndex<=0 then
		return
	end


	local nLevel  = GetLevel(varMap,varPlayer)

	if nLevel>=x700079_var_Fuben_list[varIndex].levelmin and nLevel<=x700079_var_Fuben_list[varIndex].levelmax then
		TalkAppendButton(varMap, x700079_var_FileId, strName,14,varIndex)
	end

	

	
	
	
			
end

function x700079_GetFubenName(varMap, varPlayer)

	local varIndex =0
	
	local dayweek = GetWeek()
	for varI, item in x700079_var_Fuben_list do
		varIndex = varIndex+1
		if dayweek==item.weekday then
			
			local hour,minute,sec =GetHourMinSec();
			local nowtime = hour*60+minute
	
	
			if nowtime >= item.starttime and nowtime <= item.endtime then
			
				return varIndex,item.varName
				
			end
			
			
		end
	end

	return -1," "
	
end






