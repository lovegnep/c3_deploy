				x700303_CreateBoss(varMap, iBossIndex+1) -- 幹秀和匯態boss
			elseif 3 == iBossIndex then
				x700303_ShowTipsToAll(varMap, "乏旋似姫BOSS4")
			elseif 4 == iBossIndex then
				x700303_ShowTipsToAll(varMap, "乏旋似姫BOSS5")
			elseif 5 == iBossIndex then
				x700303_ShowTipsToAll(varMap, "乏旋似姫BOSS6")
				x700303_CreateBoss(varMap, iBossIndex+1) -- 幹秀恷朔匯倖boss
			elseif x700303_g_LastBossIndex == iBossIndex then
				-- 恷朔匯倖boss棒蘭
