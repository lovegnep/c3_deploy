			objId = x700302_g_BossInfoTbl[iBossIndex+1].BossId
			step = x700302_g_BossInfoTbl[iBossIndex+1].BossStep
			fullHp = x700302_g_BossInfoTbl[iBossIndex+1].BossHp
		end
	end

	if -1 == objId or -1 == fullHp then
		return
	end

