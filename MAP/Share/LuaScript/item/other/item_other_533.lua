-- 抽奖的控制
-- 第7格1000次内最多允许出1个
x418533_g_item_1_pos = 1;
x418533_g_item_1_cnt = 10001;
x418533_g_item_1_limit = 1;
x418533_g_item_1_itemcnt = 0;
x418533_g_item_1_cur = 0;


function x418533_CheckMoneyItem(item,a)
	local jilu = 0.8; --小于此百分比数量不出
	if a == 0 then	
		x418533_g_item_1_cur = x418533_g_item_1_cur + 1;
	end
 	if item == x418533_g_item_1_pos then
		if x418533_g_item_1_cur < x418533_g_item_1_cnt then
			if x418533_g_item_1_cur > x418533_g_item_1_cnt * jilu and x418533_g_item_1_itemcnt < x418533_g_item_1_limit then
				x418533_g_item_1_itemcnt = x418533_g_item_1_itemcnt + 1;
				return item;
			else
				x418533_CheckMoneyItem(random(1,104),1);
			end
		else
			x418533_g_item_1_cur = 0;
			x418533_g_item_1_itemcnt = 0;
			x418533_CheckMoneyItem(random(1,104),1);
		end
	end	
	return item;
end

