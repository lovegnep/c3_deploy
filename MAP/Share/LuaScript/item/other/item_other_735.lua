	local tb_id = x418735_get_item_key( varMap , varPlayer );
	
	if ( tb_id == nil ) then
		return
	end
	
	if ( extid == nil ) or 
	   ( extid > x418735_array_size ) then
	   return
	end
	
	if ( tb_id > extid ) then
		Msg2Player( varMap, varPlayer, "您已经领取过该级别礼品", 8, 3 )
		return
	end
	
	if ( tb_id < extid ) then
		local key_lv = x418735_item_list[ tb_id ].lv;
		Msg2Player( varMap, varPlayer, "您需要先领取"..tostring( key_lv ).."级的特权礼包", 8, 3 );
		return
	end

	local lv = x418735_item_list[ extid ].lv;
	local cur_lv =GetLevel( varMap , varPlayer );
	local cur_metier = GetZhiye( varMap , varPlayer );
	
	
	if cur_lv < lv then
		Msg2Player( varMap, varPlayer, "您还没有达到该级别", 8, 3 )
		return
	end
	
	
	
	
	StartItemTask(varMap);
	
	local list = x418735_item_list[ extid ].array;
	local get_num = 0;
	
	for varI , s in list do
		
		if s.metier == cur_metier then
		
			for j , u in s.list do
				
				
				local varNum = u.varNum;
				local varId = u.varId;
				
				if varId == nil then

					
					local random_key = x418735_get_random_key( lv );
					
					if ( random_key ~= nil ) then
					
						local copy = {};
						local ref = x418735_item_random_list[ random_key ];
						
						for varX = 1 , x418735_array_size do
							copy[ varX ] = ref[ varX ];
						end

						local copy_l = getn( copy );
					
						for k = 1 , varNum do
							for varI = 1 , copy_l do
								local r = random( 1 , copy_l );
								local r_id = copy[ r ];
								
								if r_id ~= nil then
									copy[ r ] = nil;
									ItemAppendBind( varMap , r_id , 1 );
									get_num = get_num + 1;
									break
								end
							end
						end
						
					end

					
				else
					
					ItemAppendBind( varMap , varId , varNum );
					get_num = get_num + 1;
				end
				
			end
			
		end
	end
	
	
	local varRet = StopItemTask(varMap,varPlayer);
	
	if get_num == 0 then
		x418735_output_msg( varMap,varPlayer,"获取道具失败！" );
	end
	
	if ( varRet > 0 ) then
	
		if(DepletingUsedItem(varMap, varPlayer)) == 1 then
