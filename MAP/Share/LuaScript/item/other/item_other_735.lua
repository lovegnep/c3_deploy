			DeliverItemListSendToPlayer(varMap,varPlayer);
		else
			x418735_output_msg( varMap,varPlayer,"使用物品异常" );
		end
	else
		x418735_output_msg( varMap,varPlayer,"背包已满，无法得到物品！您需要至少有"..tostring( get_num ).."个背包空间！" );
	end
	



	
	
end











