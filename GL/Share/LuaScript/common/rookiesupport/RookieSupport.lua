--玩家进入地图增加新手扶持buff
function x802000_EnterMap(varMap, varPlayer)

		--新手扶持经验buff
		local nIndex = GetRookieInfoIndex(varMap, varPlayer, 0);
		if nIndex <= 0 then 
			local buffId, nExp, nIndex = x802000_GetRookieExp(varMap, varPlayer)
			if buffId > 0 then 
				 --增加buff
				 SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, buffId, 0);
			end
			--设置玩家身上新手扶持索引
			SetRookieInfoIndex(varMap, varPlayer, 0, nIndex);
		else
			if x802000_RookieExp[nIndex].buffId ~= nil then
			   SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, x802000_RookieExp[nIndex].buffId, 0);
			end
		end
		
		
		--掉落buff
		nIndex = GetRookieInfoIndex(varMap, varPlayer, 1);
		if nIndex <= 0 then
			 local buffId, nIndex = x802000_IsRookieDrop(varMap, varPlayer)
			 if buffId > 0 then 
			    SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, buffId, 0);
			 end
			 SetRookieInfoIndex(varMap, varPlayer, 1, nIndex);
		else
			if x802000_RookieDrop[nIndex].buffId ~= nil then 
			   SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, x802000_RookieDrop[nIndex].buffId, 0);
			end
		end
end

--玩家升级增加buff
function x802000_PlayerLevelUp(varMap, varPlayer, varLevel)
		
		--获取等级排行榜最后一名等级
		local TopListMinLevel = GetTopListInfo_MinLevel(GetWorldID( varMap, varPlayer))
		local recLevel        = TopListMinLevel - varLevel;
		
		--经验扶持buff
		--获取新手扶持经验索引
		local nIndex = GetRookieInfoIndex(varMap, varPlayer, 0);
		if nIndex <=0 then
			 --上一等级没在扶持内,重新计算当前等级是否在扶持内
			 local buffId, nExp, nIndex = x802000_GetRookieExp(varMap, varPlayer);
			 if buffId > 0 then 
			    SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, buffId, 0);
			 end
			 SetRookieInfoIndex(varMap, varPlayer, 0, nIndex);
		elseif x802000_RookieExp[nIndex].topListMinLevel ~= nil then
				x802000_CalculateRookieBuff(varMap, varPlayer, varLevel, recLevel, 0, nIndex, x802000_RookieExp)
		end
		
		 --掉落扶持buff
	   --获取新手扶持掉落索引
		nIndex = GetRookieInfoIndex(varMap, varPlayer, 1);
		if nIndex <= 0 then 
		   --重新计算是否在掉落扶持内
			 local buffId, nIndex = x802000_IsRookieDrop(varMap, varPlayer);
			 if buffId > 0 then 
			    SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, buffId, 0);
			 end
			 SetRookieInfoIndex(varMap, varPlayer, 1, nIndex);
		elseif x802000_RookieDrop[nIndex].playerMinLevel ~= nil then
			 x802000_CalculateRookieBuff(varMap, varPlayer, varLevel, recLevel, 1, nIndex, x802000_RookieDrop)
		end
	
end

--经验计算
function x802000_RookieSupport(varMap, varPlayer, selfLevel, playerExp, nIndex)
		
		if x802000_RookieExp[nIndex].Exp == nil then
		   return playerExp;
		end
		
		local exp = x802000_RookieExp[nIndex].Exp;
		
		return tonumber((exp * 0.1 * playerExp) + playerExp);
end

--是否在经验扶持内
function x802000_GetRookieExp(varMap, varPlayer)
		local varLevel = GetLevel(varMap, varPlayer)
		--获取等级排行榜最后一名的等级
		local TopListMinLevel = GetTopListInfo_MinLevel(GetWorldID( varMap, varPlayer))
		varLevel = TopListMinLevel - varLevel;
	
		if varLevel < x802000_RookieExp[1].topListMinLevel then
			return 0, 0, 0;
		end

		local buffId = 0;
		local exp		 = 0;
		local nIndex = 0;
		for varI,item in x802000_RookieExp do
				if varLevel >= item.topListMinLevel and varLevel <= item.topListMaxLevel then
					 		buffId = item.buffId;
					 		exp    = item.Exp;
					 		nIndex = varI;
					 		break;
				end
		end
		
		return buffId, exp, nIndex;
end

--是否在新手扶持掉落内
function x802000_IsRookieDrop(varMap, varPlayer)
		local varLevel = GetLevel(varMap, varPlayer)
		--获取等级排行榜最后一名的等级
		local TopListMinLevel = GetTopListInfo_MinLevel(GetWorldID( varMap, varPlayer))
		local level = TopListMinLevel - varLevel;
		if level < x802000_RookieDrop[1].topListMinLevel then
			 return 0, 0;
		end

		for k, value in x802000_RookieDrop do
				if varLevel >= value.playerMinLevel  and varLevel <= value.playerMaxLevel then
					 if level >= value.topListMinLevel and level <= value.topListMaxLevel then
							return value.buffId,k;
					 end
				end
		end
		
		return 0, 0;
end

--掉落的装备
function x802000_DropItem(varMap, varPlayer, nIndex)

		if x802000_RookieDrop[nIndex].Rate == nil then
		   return 0;
		end
		
		local varLevel = GetLevel(varMap, varPlayer)
		--获取等级排行榜最后一名的等级
		local TopListMinLevel = GetTopListInfo_MinLevel(GetWorldID( varMap, varPlayer))
		local level = TopListMinLevel - varLevel;
		if level < x802000_RookieDrop[1].topListMinLevel then
			 return 0;
		end
		
 		--是否在掉落地图内
 		local isMap = 0;
 		for i, map in x802000_RookieDrop[nIndex].MapList do
 				if varMap == map then
 					 isMap = 1;
 					 break;
 				end
 		end
 		
 		if isMap ~= 1 then 
 			 return 0;
 		end
 		
 		--是否掉落装备
		local rand = random(0, x802000_RandomMax);
		if rand > x802000_RookieDrop[nIndex].Rate then
			 return 0;
		end
		
		local ItemCount = 0;
		for i, item in x802000_RookieDrop[nIndex].ItemList do
				ItemCount = i;
		end
		--随即哪件装备
		rand = random(1, ItemCount);
		
		return x802000_RookieDrop[nIndex].ItemList[rand];
		
end

--几星装备
function x802000_ItemStar(varMap, varPlayer, nIndex)

		if x802000_RookieDrop[nIndex].MinStar == nil then
		   return 0;
		end
		
		local varLevel = GetLevel(varMap, varPlayer)
		--获取等级排行榜最后一名的等级
		local TopListMinLevel = GetTopListInfo_MinLevel(GetWorldID( varMap, varPlayer))
		local level = TopListMinLevel - varLevel;
		if level < x802000_RookieDrop[1].topListMinLevel then
		   return 0;
		end
		
 		--随即装备星
 		local star = random(x802000_RookieDrop[nIndex].MinStar, x802000_RookieDrop[nIndex].MaxStar);
 		if star < x802000_RookieDrop[nIndex].MinStar or star > x802000_RookieDrop[nIndex].MaxStar then
 			 return 0;
 		end
 		
 		return star;

end

--计算升级后是否在扶持内
function x802000_CalculateRookieBuff(varMap, varPlayer, varLevel, recLevel, nType, nIndex, value)

		--经验buf
		if nType == 0 then 
		  --先计算当前等级是否还在上一扶持内
			if recLevel >= value[nIndex].topListMinLevel and recLevel <= value[nIndex].topListMaxLevel then
			   return;
			end
			
			 --当前等级不在上一扶持内
			 --计算当前等级是否在上一扶持索引的下一个
			 if value[nIndex + 1].topListMinLevel ~= nil then
				  if recLevel >= value[nIndex + 1].topListMinLevel and recLevel <= value[nIndex + 1].topListMaxLevel then
						 SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, value[nIndex + 1].buffId, 0);
						 --设置经验扶持索引
			       SetRookieInfoIndex(varMap, varPlayer, nType, nIndex + 1);
					else
						 x802000_DelBuffSetIndex(varMap, varPlayer, value[nIndex].buffId, nType, 0)
			    end
			 else
					 x802000_DelBuffSetIndex(varMap, varPlayer, value[nIndex].buffId, nType, 0)
			 end
		end
		
		--掉落buf
		if nType == 1 then
		  --先计算当前等级是否还在上一扶持内
			if recLevel >= value[nIndex].topListMinLevel and recLevel <= value[nIndex].topListMaxLevel and
			   varLevel >= value[nIndex].playerMinLevel  and varLevel <= value[nIndex].playerMaxLevel  then
			   		return;
			end
			
			 --不在上一扶持内
		   if value[nIndex + 1].topListMinLevel ~= nil then
		      if recLevel >= value[nIndex + 1].topListMinLevel and recLevel <= value[nIndex + 1].topListMaxLevel and
		         varLevel >= value[nIndex + 1].playerMinLevel  and varLevel <= value[nIndex + 1].playerMaxLevel  then
		         	 SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, value[nIndex + 1].buffId, 0);
						   --设置经验扶持索引
			         SetRookieInfoIndex(varMap, varPlayer, nType, nIndex + 1);
			    else
			     		x802000_DelBuffSetIndex(varMap, varPlayer, value[nIndex].buffId, nType, 0)
		   		end
		   else
					x802000_DelBuffSetIndex(varMap, varPlayer, value[nIndex].buffId, nType, 0)
		   end
		end
end

--删除buff并设置扶持索引
function x802000_DelBuffSetIndex(varMap, varPlayer, buffId, nType, nIndex)
   CancelSpecificImpact(varMap, varPlayer, buffId);
   --设置经验扶持索引
   SetRookieInfoIndex(varMap, varPlayer, nType, nIndex);
end
