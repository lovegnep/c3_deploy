function x570044_ProcChangeProfession( varMap, varPlayer, varTalknpc, varDestProfession )
    
    local varLevel = GetLevel( varMap, varPlayer)
     if varLevel < 70 then
        Msg2Player( varMap, varPlayer, "70级以上才能进行职业转换", 8, 3)
        return
    end
    
    local myProfession = GetZhiye( varMap, varPlayer)
    local targetProf = nil
    local changeItem = nil
    for varI, item in x570044_var_ChangeConfiguration do
        if myProfession == item.professionId1 or myProfession == item.professionId2 then
            if myProfession == item.professionId1 then
                targetProf = item.professionId2
            else
                targetProf = item.professionId1
            end
            for j, subitem in item.cost do
                if varLevel >= subitem.minLevel and varLevel < subitem.maxLevel then
                    changeItem = subitem
                    break
                end
            end
            break
        end
    end

    if changeItem == nil or targetProf == nil then
        return
    end

	local money = changeItem.moneyCoff * varLevel
	if GetMoney( varMap, varPlayer, 1) < money and GetMoney( varMap, varPlayer, 0) < money then
        Msg2Player( varMap, varPlayer, "你的银卡或现银不足", 8, 3)
		return
	end
    if GetItemCountInBag( varMap, varPlayer, changeItem.varItem) < changeItem.varCount then
        Msg2Player( varMap, varPlayer, format( "你没有@item_%d", changeItem.varItem), 8, 3)
        return
    end


    local mtype = 1
    if GetSillerMode( varMap, varPlayer ) == 1 then
        if CostMoney( varMap, varPlayer, 0, money) ~= 1 then
            if CostMoney(varMap, varPlayer, 1, money) ~= 1 then
                Msg2Player( varMap, varPlayer, "扣费失败，请稍候再试", 8, 3)
                return
            end
            mtype = 1
        end
        mtype = 0
    else
        if CostMoney( varMap, varPlayer, 1, money) ~= 1 then
            if CostMoney(varMap, varPlayer, 0, money) ~= 1 then
                Msg2Player( varMap, varPlayer, "扣费失败，请稍候再试", 8, 3)
                return
            end
            mtype = 0
        end
        mtype = 1
    end

    if DelItem( varMap, varPlayer, changeItem.varItem, changeItem.varCount) == 0 then
        Msg2Player( varMap, varPlayer, "提交物品失败，请稍候再试", 8, 3)
        AddMoney( varMap, varPlayer, mtype, money)
        return
    end

    if IsNude( varMap, varPlayer) == 0 then
        Msg2Player( varMap, varPlayer, "必须清除身上的所有装备才能进行转职", 8, 3)
        return
    end

    for varI, item in x570044_var_DispelSkillList[ myProfession+1 ] do
        DispelSpecificImpact( varMap, varPlayer, item, 1)
    end
    
    ChangeProfession( varMap, varPlayer, targetProf )

	StartTalkTask( varMap)
        TalkAppendString( varMap, "#Y职业转换" )
        TalkAppendString( varMap, "转换完成。" )
	StopTalkTask()
	DeliverTalkMenu( varMap, varPlayer, varTalknpc)


    SendSpecificImpactToUnit( varMap, varPlayer, varPlayer, varPlayer, 9801, 200)


    KickPlayer( varMap, varPlayer, 2)

    SetKickTime( varMap, varPlayer, 10000)
end

function x570044_ProcEnumEvent( varMap, varPlayer, varTalknpc, varQuest)
    TalkAppendButton( varMap, x570044_var_FileId, "职业转换", 3, 1)
end

function x570044_ProcEventEntry( varMap, varPlayer, varTalknpc, idScript, buttonIdx)
    local varLevel = GetLevel( varMap, varPlayer)
    if varLevel < 70 then
        Msg2Player( varMap, varPlayer, "70级以上才能进行职业转换", 8, 3)
        return
    end

    if buttonIdx == 1 then

        local myProfession = GetZhiye( varMap, varPlayer)
        local playerLevel = GetLevel( varMap, varPlayer)
        
        for varI, item in x570044_var_ChangeConfiguration do
            if myProfession == item.professionId1 or myProfession == item.professionId2 then
                for j, subitem in item.cost do
                    if varLevel >= subitem.minLevel and varLevel < subitem.maxLevel then
                        OpenChangeProfessionUI( varMap, varPlayer, varTalknpc, idScript, subitem.varItem, subitem.varCount, subitem.moneyCoff * playerLevel , 0 )
                        break
                    end
                end
                break
            end
        end
    end
end

