function x300501_PositionUseItem( varMap, varPlayer, varBagIdx, varImpact, varQuest )

	varQuest = x300501_GetRandQuestId(varMap, varPlayer);

	local varItem = GetItemTableIndexByIndex( varMap, varPlayer, varBagIdx ) 
	local varQuest = GetQuestIDByItemIDNM( varMap, varPlayer, varItem )

	if varQuest == -1 then 	
		return 0
	end

	if IsHaveQuestNM( varMap, varPlayer, varQuest ) == 0 then	
		return 0
	end

	local scene, posx, posz, radii, QuestIdx, dispStr, bDispOther, otherStr = GetQuestUseItemInfoNM( varMap, varPlayer, varQuest, varItem )
	if scene == -1 or varMap ~= scene then 
		return 0
	end

	if posx > 0 then 
		local PlayerPosX,PlayerPosZ = GetWorldPos( varMap, varPlayer )

		local distanceRet = radii * radii - (posx - PlayerPosX) * (posx - PlayerPosX) - (posz - PlayerPosZ) * (posz - PlayerPosZ)
		if distanceRet <= 0 then
			local varStr = format("你应该在@sceneid_%d[%d,%d]使用此物品！", scene, posx, posz )
			Msg2Player(varMap, varPlayer, varStr, 8, 3);
			return 0
		end
	end

	if x300501_GetMP(varMap, varPlayer, varQuest, QuestIdx) == 1 then
		return 0
	end

	
	if EraseItem(varMap, varPlayer, varBagIdx) ~= 1 then return 0 end

	
	x300501_SetMP(varMap, varPlayer, varQuest, QuestIdx, 1);
	Msg2Player(varMap, varPlayer, dispStr, 8, 3);

	if bDispOther > 0 then
		
		Msg2Player(varMap, varPlayer, otherStr, 8, 0);
	end

	
	if varImpact ~= -1 then
		SendSpecificImpactToUnit(varMap, varPlayer, varPlayer, varPlayer, varImpact, 0);
	end

	
	local varQuestIdx = GetQuestIndexByID(varMap, varPlayer, varQuest);
	local MisCareNPC = QuestCareNPCNM( varMap, varPlayer, varQuestIdx )
	if MisCareNPC == 0 then 
		local varRet = QuestCheckSubmitNM( varMap, varPlayer, 0, varQuest, varQuestIdx ) 
		if varRet > 0 then 
			return x300501_QuestCanSubmit( varMap, varPlayer, 0, -1, varQuest )
		end
	end

	return 1
end


