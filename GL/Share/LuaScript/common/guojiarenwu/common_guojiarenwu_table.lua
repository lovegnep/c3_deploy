function x300501_GenerateRandomQuest(varMap, varPlayer, varTalknpc)

	local varQuest = x300501_var_DefaultQuestId;
	local varLevel = GetLevel(varMap, varPlayer);
	local oldtype = x300501_GetMD(varMap, varPlayer, MD_GUOJIARENWU_RANDMISSIONTYPE)
	local newtype = 0;
	while 1 do
		local randval = random(0, 99);

		for varI, setitem in x300501_var_QuestIdList do
			if varLevel >= setitem.varLevel.min and varLevel < setitem.varLevel.max then
				for j, rateitem in setitem.rate do
					if randval < rateitem then
						newtype = j;
						break;
					end
				end
				randval = random(1, getn(setitem.varQuest[newtype]));
				varQuest = setitem.varQuest[newtype][randval];
				break;
			end
		end

		if newtype == 0 then	
			return 0;
		end

		if IsBusMember(varMap, varPlayer) == 0 or newtype ~= 7 then	
			if newtype ~= oldtype then
				break;
			end
		end
	end

	
	
	if varQuest < x300501_var_QuestRangeStart or varQuest > x300501_var_QuestRangeEnd then
		varQuest = x300501_var_QuestRangeStart;
	end
	




	
	
	
	
	
	
	
	

	x300501_SetMD (varMap, varPlayer, MD_GUOJIARENWU_RANDMISSIONID, varQuest)
	x300501_SetMD (varMap, varPlayer, MD_GUOJIARENWU_RANDMISSIONTYPE, newtype)
	return varQuest, newtype;
end

