function x300501_ProcQuestAbandon( varMap, varPlayer, varQuest )
	varQuest = x300501_GetRandQuestId(varMap, varPlayer);
	if (varQuest == 0 or IsHaveQuestNM( varMap, varPlayer, varQuest ) == 0 ) then
		
		x300501_DelAllWrongQuest(varMap, varPlayer)
		return 0
	end
	local varRet = 0

	if x300501_IsDeliver(varQuest) == 1 then 
		
		local varCount = GetItemCount(varMap,varPlayer,x300501_var_DeliverItemID)
		if varCount > x300501_var_DeliverItemCount then
			varCount = x300501_var_DeliverItemCount
		end
		
		if varCount > 0 and 0 == DelItem(varMap, varPlayer, x300501_var_DeliverItemID, varCount) then
			Msg2Player(varMap, varPlayer, "无法扣除任务道具，请稍候重试", 8, 3)
			Msg2Player(varMap, varPlayer, "无法扣除任务道具，请稍候重试", 8, 2)
			return 0
		end
	end
	
	local  varQuestIdx = GetQuestIndexByID(varMap, varPlayer, varQuest);
	varRet = DelQuestNM( varMap, varPlayer, varQuest )
	if varRet == 1 then

		QuestUnCom( varMap, varPlayer, x300501_var_VirtualQuestId_Low)
		QuestUnCom( varMap, varPlayer, x300501_var_VirtualQuestId_High)
		x300501_SetMD(varMap, varPlayer, MD_GUOJIARENWU_LASTDAY, GetDayOfYear())
		x300501_SetMD(varMap, varPlayer, MD_GUOJIARENWU_ABANDON, GetMinOfDay()+1)
		
		x300501_SetMD(varMap, varPlayer, MD_GUOJIARENWU_RANDMISSIONID, 0)
		
		if x300501_GetMD(varMap, varPlayer, MD_GUOJIARENWU_HIGH) < x300501_HighBonusCircleCount then
			x300501_SetMD(varMap, varPlayer, MD_GUOJIARENWU_HIGH, x300501_HighBonusCircleCount)
			 
			x300501_SetMD(varMap, varPlayer, MD_GUOJIARENWU_CIRCLE, 0)
		else
			
			
			x300501_SetRounds(varMap, varPlayer)
			
		end

		x300501_DisplayAbandonMessage(varMap, varPlayer, varQuest);
	end

	return varRet;
end

