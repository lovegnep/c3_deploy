function x300501_ProcQuestHorseChanged(varMap, varPlayer, horseId, varQuest)

	if x300501_IsCollectHorse(varQuest) == 0 then
		return
	end

	if (x300501_IsNeededHorse(varQuest, horseId) == 0) then
		return
	end

	if IsHaveHorse(varMap, varPlayer, horseId) > 0 then
		
		local varQuestIdx = GetQuestIndexByID( varMap, varPlayer, varQuest );
		if (GetQuestParam(varMap, varPlayer, varQuestIdx, 7) == 0) then
			Msg2Player(varMap, varPlayer, "任务已经完成，快点回去汇报吧！", 8, 3)
		end
		SetQuestByIndex(varMap,varPlayer,varQuestIdx,7,1)
		SetQuestByIndex(varMap,varPlayer,varQuestIdx,0,1)
		return
	end

	
	x300501_IfHaveNeededHorse (varMap, varPlayer, varQuest)

end

function x300501_IsNeededHorse(varQuest, horseId)

	if x300501_IsCollectHorse(varQuest) == 0 then
		return 0
	end

	local kindtype = mod(horseId, 10)
	if (varQuest < x300501_var_CollectHorse_Young) then
		if (kindtype >= 5) then
			return 0
		end
		local group = floor(horseId / 100)
		if (group ~= varQuest - x300501_var_CollectHorse_Quest_ID + 2 ) then
			return 0
		end
	else
		if (kindtype > 5) then
			return 0
		end
		local group = floor(horseId / 10)
		if (group > 5) then
			return 0
		end
	end

	return 1
end

function x300501_IfHaveNeededHorse(varMap, varPlayer, varQuest)

	local cansubmit = 0
	for varI=0,5 do
		local horseId = GetHorseDataID(varMap, varPlayer, varI)
		if (horseId > 0) then
			cansubmit = x300501_IsNeededHorse(varQuest, horseId)
			if (cansubmit == 1) then
				break
			end
		end
	end

	if (cansubmit == 1) then
		
		local varQuestIdx = GetQuestIndexByID( varMap, varPlayer, varQuest );
		SetQuestByIndex(varMap,varPlayer,varQuestIdx,7,1)
		SetQuestByIndex(varMap,varPlayer,varQuestIdx,0,1)
	else
		
		local varQuestIdx = GetQuestIndexByID( varMap, varPlayer, varQuest );
		SetQuestByIndex(varMap,varPlayer,varQuestIdx,7,0)
		SetQuestByIndex(varMap,varPlayer,varQuestIdx,0,0)	
	end

	return cansubmit
end

