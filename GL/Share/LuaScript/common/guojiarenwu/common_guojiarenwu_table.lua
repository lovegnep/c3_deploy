function x300501_CheckAbandonPenaltyOff ( varMap, varPlayer, varTalknpc )

	local curMinute = GetMinOfDay() + 1	
	local curDay = GetDayOfYear()		

	local varLastday =  x300501_GetMD(varMap, varPlayer, MD_GUOJIARENWU_LASTDAY)
	local lastMinute =  x300501_GetMD(varMap, varPlayer, MD_GUOJIARENWU_ABANDON)

	if (lastMinute == 0) then 
		return 1;
	end

	local bCanAccept = 1
	if (varLastday == curDay) then		
		if (curMinute >= lastMinute)  and  (curMinute - lastMinute < x300501_var_AbandonTime) then 
			bCanAccept = 0
		end
	elseif (curDay - varLastday == 1) or (varLastday == 365 and curDay == 0) then	
		if 1441 - lastMinute + curMinute <= x300501_var_AbandonTime then
			bCanAccept = 0
		end
	end
	

	if (bCanAccept == 0) then
		
		local varStr = format("#Y"..x300501_var_QuestName.."#W\n\t你放弃任务不足%d分钟，不能接受任务！" , x300501_var_AbandonTime);
		StartTalkTask(varMap);
			TalkAppendString(varMap, varStr);
		StopTalkTask(varMap);
		DeliverTalkMenu(varMap, varPlayer, varTalknpc);
		return 0
	end

	
	if( lastMinute ~= 0 ) then 
		x300501_SetMD(varMap, varPlayer, MD_GUOJIARENWU_ABANDON, 0)
	end

	return 1;
end


