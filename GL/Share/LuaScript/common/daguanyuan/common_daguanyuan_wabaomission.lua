function x310364_ProcQuestAccept( varMap, varPlayer, varTalknpc, varQuest)
    if GetGameOpenById( x310364_var_IsEnableId) <= 0 then
        return
    end

    local day, week = GetWeek()
    if IsHaveQuest( varMap, varPlayer, x310364_var_QuestId) > 0 then
    	if x310364_GetMD( varMap, varPlayer, MD_WABAO_WEEK) == week then
            if day == 6 and x310364_GetMD( varMap, varPlayer, MD_WABAO_FINISHED) >= 2 then
                return
            end
            if day == 0 and x310364_GetMD( varMap, varPlayer, MD_WABAO_TIMES) >= 2 then
                return
            end
        end

        local varLevel = GetLevel( varMap, varPlayer)
        local varExp = x310364_var_finishxp *varLevel
        local varQuestIdx = GetQuestIndexByID( varMap, varPlayer, x310364_var_QuestId)
        local gtime = GetQuestParam( varMap, varPlayer, varQuestIdx, x310364_MP_TARGET3)
        AddExp( varMap, varPlayer, varExp)
        Msg2Player(varMap, varPlayer, "您完成了任务"..x310364_var_QuestName.."！", 0, 2)
        Msg2Player(varMap, varPlayer, "您完成了任务"..x310364_var_QuestName.."！", 0, 3)
        Msg2Player(varMap, varPlayer, "#Y获得#R经验"..varExp.."#Y的奖励", 0, 2)			 
        DelQuest( varMap, varPlayer, x310364_var_QuestId)
        GamePlayScriptLog(varMap, varPlayer, 1672)
        
        local useTime = GetCurrentTime() - gtime
        local needsort = 1
        local oldTime = x310364_GetMD( varMap, varPlayer, MD_WABAO_USETIME)
        if useTime < x310364_var_MaxFinishTime then
            if useTime < oldTime then
                x310364_SetMD( varMap, varPlayer, MD_WABAO_USETIME, useTime)
            else
                useTime = oldTime
            end
        end
        if x310364_GetMD( varMap, varPlayer, MD_WABAO_WEEK) == week then
            if day == 6 then
                if x310364_GetMD( varMap, varPlayer, MD_WABAO_DAYFLAG) == 1 then
                    local times = x310364_GetMD( varMap, varPlayer, MD_WABAO_FINISHED) + 1
                    x310364_SetMD( varMap, varPlayer, MD_WABAO_FINISHED, times)
                else
                    x310364_SetMD( varMap, varPlayer, MD_WABAO_FINISHED, 1)
                     
                    useTime = x310364_var_MaxFinishTime
                    x310364_SetMD( varMap, varPlayer, MD_WABAO_USETIME, x310364_var_MaxFinishTime)
                    needsort = 0
                end
            elseif day == 0 then
                if x310364_GetMD( varMap, varPlayer, MD_WABAO_DAYFLAG) == 0 then
                    local times = x310364_GetMD( varMap, varPlayer, MD_WABAO_TIMES) + 1
                    x310364_SetMD( varMap, varPlayer, MD_WABAO_TIMES, times)
                else
                    x310364_SetMD( varMap, varPlayer, MD_WABAO_TIMES, 1)
                     
                    useTime = x310364_var_MaxFinishTime
                    x310364_SetMD( varMap, varPlayer, MD_WABAO_USETIME, x310364_var_MaxFinishTime)
                    needsort = 0
                end
            end
        else
            
            if day == 6 then
                x310364_SetMD( varMap, varPlayer, MD_WABAO_FINISHED, 1)
            end
            if day == 0 then
                x310364_SetMD( varMap, varPlayer, MD_WABAO_TIMES, 1)
            end
            
            useTime = x310364_var_MaxFinishTime
            x310364_SetMD( varMap, varPlayer, MD_WABAO_USETIME, x310364_var_MaxFinishTime)
            needsort = 0
        end
        if needsort == 1 then
            
            local b = 0
            local bsort = 1
            for varI, item in x310364_var_Top20Player do
                
                if item.guid == GetGUID( varMap, varPlayer) then 
                    b = 1
                    if item.costTime > useTime then
                        
                        item.costTime = useTime
                    end
                    break
                end
            end
            if b == 0 then
                local varCount = getn( x310364_var_Top20Player)
                if varCount >= 20 then
                    
                    if useTime < x310364_var_Top20Player[ 20].costTime then
                        x310364_var_Top20Player[ 20] = { guid = GetGUID( varMap, varPlayer), costTime = useTime, varName = GetName( varMap, varPlayer), getFlag = 0 }
                    else
                        
                        bsort = 0
                    end
                else
                    x310364_var_Top20Player[ varCount + 1] = { guid = GetGUID( varMap, varPlayer), costTime = useTime, varName = GetName( varMap, varPlayer), getFlag = 0 }
                end
            end
            if bsort == 1 then
                
                sort( x310364_var_Top20Player, function ( a, b) return a.costTime < b.costTime end)
            end
        else
            Msg2Player( varMap, varPlayer, "不是当天领取任务无法参加挖宝大作战排名", 8, 3)
        end
        if random( 1, 100) <= 5 then
            StartItemTask( varMap)
            ItemAppend( varMap, x310364_var_RewardTable[ random( 1, 3) ], 1)
            if StopItemTask( varMap, varPlayer) == 0 then
                Msg2Player( varMap, varPlayer, "背包已满，无法获得额外奖励物品", 8, 3)
            else
                DeliverItemListSendToPlayer( varMap, varPlayer)
            end
        end
        x310364_SetMD( varMap, varPlayer, MD_WABAO_WEEK, week)
    else
        local minute = GetMinOfDay()
        if minute < 720 or minute > 1020 then
            Msg2Player( varMap, varPlayer, "此任务只有周六、日下午#G12：00——17：00#cffcc00可以领取", 8, 3)
            return
        end
        if x310364_GetMD( varMap, varPlayer, MD_WABAO_WEEK) == week then
            if day == 6 and x310364_GetMD( varMap, varPlayer, MD_WABAO_FINISHED) >= 2 then
                Msg2Player( varMap, varPlayer, "今天不能再领取此任务了", 8, 3)
                return
            end
            if day == 0 and x310364_GetMD( varMap, varPlayer, MD_WABAO_TIMES) >= 2 then
                Msg2Player( varMap, varPlayer, "今天不能再领取此任务了", 8, 3)
                return
            end
        else
            x310364_SetMD( varMap, varPlayer, MD_WABAO_WEEK, week)
            x310364_SetMD( varMap, varPlayer, MD_WABAO_FINISHED, 0)
            x310364_SetMD( varMap, varPlayer, MD_WABAO_TIMES, 0)
            x310364_SetMD( varMap, varPlayer, MD_WABAO_DUIHUAN1, 0)
            x310364_SetMD( varMap, varPlayer, MD_WABAO_USETIME, x310364_var_MaxFinishTime)
        end
        if AddQuest( varMap, varPlayer, x310364_var_QuestId, x310364_var_FileId, 0, 0, 0, 0) == 0 then
            Msg2Player( varMap, varPlayer, "任务已满，添加任务失败", 8, 3)
            return
        else
        	Msg2Player(varMap, varPlayer, "您接受了任务"..x310364_var_QuestName.."！", 0, 2)
		  		Msg2Player(varMap, varPlayer, "您接受了任务"..x310364_var_QuestName.."！", 0, 3)    
        end
        if day == 6 then
            x310364_SetMD( varMap, varPlayer, MD_WABAO_DAYFLAG, 1)
        elseif day == 0 then
            x310364_SetMD( varMap, varPlayer, MD_WABAO_DAYFLAG, 0)
        end
        GamePlayScriptLog(varMap, varPlayer, 1671)
        local varQuestIdx = GetQuestIndexByID( varMap, varPlayer, x310364_var_QuestId)
        SetQuestByIndex( varMap, varPlayer, varQuestIdx, x310364_MP_TARGET1, random( 1, getn( x310364_var_PosTable) ) )
        SetQuestByIndex( varMap, varPlayer, varQuestIdx, x310364_MP_TARGET2, 0)
        SetQuestByIndex( varMap, varPlayer, varQuestIdx, x310364_MP_TARGET3, floor( GetCurrentTime() ) )
