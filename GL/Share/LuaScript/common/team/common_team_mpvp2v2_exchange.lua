		local nTitleID = 0
		if nTitleLevel > 0 then
			
			DeleteTitle(varMap,varPlayer, x303105_var_ChangeintoItemID[GetGUID(varMap, varPlayer)][varIndex].varId)
			if varIndex + 1 <= getn(x303105_var_ChangeintoItemID[GetGUID(varMap, varPlayer)]) then
				nTitleID = x303105_var_ChangeintoItemID[GetGUID(varMap, varPlayer)][varIndex + 1].varId
			else
				nTitleID = x303105_var_ChangeintoItemID[GetGUID(varMap, varPlayer)][varIndex].varId
			end
		else
			nTitleID = x303105_var_ChangeintoItemID[GetGUID(varMap, varPlayer)][1].varId
		end
		
		
		AwardTitle(varMap,varPlayer, nTitleID)	
		
		WriteLog(1, format("x303105_ProcAcceptCheck 1 GetTitle OK: varMap(%d) GUID(%d) GetTitleID(%d)", 
   			varMap, GetGUID(varMap, varPlayer), nTitleID))
   			
	else
		-- 检测背包是否有空格
		if GetBagSpace(varMap, varPlayer) <= 0 then
			Msg2Player(varMap, varPlayer, "背包剩余空间不足，无法得到兑换物品", 8, 3)
			return
		end
		
		for varI, item in x303105_var_Need_DstItemInfo do
			if item.varItem == x303105_var_ChangeintoItemID[GetGUID(varMap, varPlayer)] then
				if nSrcItemNum < item.neednum then
					Msg2Player(varMap, varPlayer, "你的英勇勋章数量不足，无法兑换", 8, 3)
					Msg2Player(varMap, varPlayer, "你的英勇勋章数量不足，无法兑换", 8, 2)
					return
				else 
						 
					 if 0 == DelItem(varMap, varPlayer, x303105_var_Need_SrcItemID, item.neednum) then
					 		return
					 end
					 
					StartItemTask(varMap)
					ItemAppendBind(varMap, item.varItem, item.varNum)
					if StopItemTask(varMap, varPlayer) > 0 then
						DeliverItemListSendToPlayer(varMap,varPlayer)
					     
					  WriteLog(1, format("x303105_ProcAcceptCheck 3 DeleteItem OK: varMap(%d) GUID(%d) DeleteItemID(%d) DeleteItemNum(%d)", 
    					 	varMap, GetGUID(varMap, varPlayer), x303105_var_Need_SrcItemID, item.neednum))
					else
						StartItemTask(varMap)
						ItemAppendBind(varMap, item.varItem, item.varNum)
						StopItemTask(varMap, varPlayer)
						DeliverItemListSendToPlayer(varMap,varPlayer)
						
						Msg2Player(varMap, varPlayer, "背包剩余空间不足，无法得到兑换物品", 8, 3)
						return
					end
				end
			end
		end
	end
	
end





function x303105_ProcChangeSelfTitle(varMap, varPlayer, titleId, type)

	if type ~= 2 then
		return
	end
	
	
	for varI, item in x303105_var_TitleLevel do
		for j, title in item.varId do
			if title.varId == titleId then
				if j ~= 1 then
					AwardTitle(varMap, varPlayer, item.varId[j - 1].varId)
					Msg2Player(varMap, varPlayer, format("由于【%s】称号过期，降级为【%s】", title.varName, item.varId[j - 1].varName), 8, 3)
				end
				return
			end
		end
	end
end
