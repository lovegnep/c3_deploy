function x303006_GenBattleResult( varMap )

	
	if x303006_SceneInfo[varMap].ISCLOSING == 1 then
		return
	end
	
	local nResult = -1 

	
	
	
	
	
	if  x303006_CheckTeamA_AllLeave( varMap ) == 0 and x303006_CheckTeamB_AllLeave( varMap ) == 1 then
		nResult = 0
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	
	if  x303006_CheckTeamA_AllLeave( varMap ) == 1 and x303006_CheckTeamB_AllLeave( varMap ) == 0 then
		nResult = 1
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	
	
	
	if x303006_TeamInfoA_Score[varMap] == x303006_var_MaxScore and x303006_TeamInfoB_Score[varMap] < x303006_var_MaxScore then
		nResult = 0
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	if x303006_TeamInfoA_Score[varMap] < x303006_var_MaxScore and x303006_TeamInfoB_Score[varMap] == x303006_var_MaxScore then
		nResult = 1
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	
	
	
	if x303006_BossDieFlag[varMap] == 1 then
		
		nResult = 1
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	if x303006_BossDieFlag[varMap] == 2 then
		
		nResult = 0
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	
	
	
	if x303006_TeamInfoA_Score[varMap] == x303006_var_MaxScore and x303006_TeamInfoB_Score[varMap] == x303006_var_MaxScore then
		nResult = -1
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	if x303006_TeamInfoA_Score[varMap] > x303006_TeamInfoB_Score[varMap]  then
		nResult = 0
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	if x303006_TeamInfoA_Score[varMap] < x303006_TeamInfoB_Score[varMap] then
		nResult = 1
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	if x303006_TeamInfoA_Score[varMap] == x303006_TeamInfoB_Score[varMap] then
		nResult = -1
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
	
	
	
	
	if  x303006_CheckTeamA_AllLeave( varMap ) == 0 and x303006_CheckTeamB_AllLeave( varMap ) == 0 then
		nResult = -1
		x303006_GiveBattleBonus( varMap,nResult )
		return
	end
		
end





