function x303006_UpdateBattleMapOtherPlayerInfoToPlayer( varMap, self, player, ta, varCount )
	local map_size = 256
	local pic, bshow, varX, z, xx, zz
	
	for varI = 1, 12 do
	
		if player[varI].varId ~= -1 or player[varI].oldid ~= -1 then
			bshow = 0
			if player[varI].varId ~= -1 and IsHaveSpecificImpact( varMap, player[varI].varId, 7978 ) <= 0 then 
			
				if self.camp == player[varI].camp then
					bshow = 1
				else 
					
					for j = 1, 12 do
						if player[j].varId ~= -1 and player[varI].varId ~= player[j].varId and player[j].camp == self.camp then
							xx = player[varI].varX - player[j].varX
							zz = player[varI].z - player[j].z
							if xx*xx + zz*zz <= 25*25 then
								bshow = 1
								break
							end
						end
					end
					
					if bshow ~= 1 then
						for k, item in ta do
							if item.varId ~= -1 and IsObjValid(varMap,item.varId) == 1 and item.hp > 0 then
								if self.camp == item.camp or self.camp == item.camp-23 then
									xx = item.varX - player[varI].varX
									zz = item.z - player[varI].z
									if xx*xx + zz*zz <= 25*25 then
										bshow = 1
										break
									end
								end
							end
						end
					end
				end
			end	
		
			if bshow == 1 then
				if self.varId == player[varI].varId then
					pic = x303006_var_UpdateBattleMap_Pic_Self
				elseif self.camp == player[varI].camp then
					pic = x303006_var_UpdateBattleMap_Pic_Team
				else
					pic = x303006_var_UpdateBattleMap_Pic_OtherTeam
				end
			
				varX = player[varI].varX/map_size
				z = player[varI].z/map_size
				if player[varI].oldid == -1 then
					SendBattleMapObj(varMap, self.varId, varCount, x303006_var_UpdateBattleMap_All, pic, varX, z)
				else
					if player[varI].hp >= player[varI].oldhp then
						if mod(x303006_var_Texiao[varMap], 3) == 1 then
							SendBattleMapObj(varMap, self.varId, varCount+160, x303006_var_UpdateBattleMap_Pos, 0, varX, z)
						else
							SendBattleMapObj(varMap, self.varId, varCount, x303006_var_UpdateBattleMap_Pos, 0, varX, z)
						end
					else
						SendBattleMapObj(varMap, self.varId, varCount+80, x303006_var_UpdateBattleMap_Pos, 0, varX, z)
					end
				end
			else
				SendBattleMapObj(varMap, self.varId, varCount, x303006_var_UpdateBattleMap_Hide, 0, -1, -1)
			end
		end
		varCount = varCount+1
	end
	return varCount		
end

