function x303006_GiveAroundPlayerBattleMoney(varMap,varPlayer,nBouns)

	local nBounsCamp = GetMonsterCamp(varMap,varPlayer)
	if nBounsCamp ~= 5 and nBounsCamp ~= 6 then
		return
	end
	
	if nBounsCamp == 5 then 
		nBounsCamp = 6
	elseif nBounsCamp == 6 then 
		nBounsCamp = 5 
	end
	
	local playerTB = {}
	local playerCount = 0
	
	local humancount = GetScenePlayerCount(varMap);
	for	varI = 0, humancount - 1 do
		local humanId = GetScenePlayerObjId(varMap, varI);

		
		if IsPlayerStateNormal( varMap,humanId ) == 1 and GetHp(varMap,humanId) > 0  then
		
			
			local nDist = GetDist(varMap,varPlayer,humanId,20)
			if  nDist <= 20 then
				
				local nCamp = GetCurCamp( varMap,humanId )
				if nCamp == nBounsCamp then
					playerCount = playerCount + 1
					playerTB[playerCount] = humanId
				end
			end
			
			
		end
	end
	
	if playerCount <= 0 then
		return
	end
	
	local nResultBouns = nBouns / playerCount
	for varI = 1,playerCount do
		x303006_AddBattleMoney(varMap,playerTB[varI],nResultBouns)
	end
end


