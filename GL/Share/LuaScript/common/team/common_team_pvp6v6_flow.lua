function x303006_ProcBattleState_BattleScene(varMap,varState)

	
    local nBattleScene = x303006_GetBattleSceneType( varMap )
    if  nBattleScene <= 0 then
        return
    end
    
    
    local nBattleSceneValid = x303006_IsBattleSceneValid(varMap)
    if nBattleSceneValid ~= 1 then
    	return	
    end

    if varState == x303006_var_State_Signup then
    	
    	
    	if x303006_var_CurrentState[varMap] == x303006_var_State_Begin then
    		return
    	end
    	
        if x303006_var_CurrentState[varMap] ~= x303006_var_State_Signup then
            WriteLog(1,format("P6BL:x303006_var_State_Signup,varMap=%d,2 ",varMap  ))
        end
        
        x303006_var_CurrentState[varMap] = x303006_var_State_Signup
        PVP6v6RegisterScene( varMap )

    elseif varState == x303006_var_State_Begin then

        
        if x303006_var_CurrentState[varMap] ~= x303006_var_State_Begin then
            WriteLog(1,format("P6BL:x303006_var_State_Begin,varMap=%d,2",varMap ))
        end

        
        x303006_var_CurrentState[varMap] = x303006_var_State_Begin
        x303006_ProcBattleSceneReady( varMap,x303006_var_LimitTotalHoldTime )

    elseif varState == x303006_var_State_End then

        if x303006_var_CurrentState[varMap] > x303006_var_State_Begin or x303006_var_CurrentState[varMap] < x303006_var_State_Signup then
            
            
            WriteLog(1,format("P6BL:x303006_var_State_End,Result:nState > x303006_var_State_Begin or nState < x303006_var_State_Signup  varMap=%d,varState=%d,2",varMap, x303006_var_CurrentState[varMap] ))

            
            x303006_ProcBattleSceneClose( varMap,0 )

            
            WriteLog(1,format("P6BL:x303006_var_State_End, varMap=%d,2",varMap ))
            return
        end

        
