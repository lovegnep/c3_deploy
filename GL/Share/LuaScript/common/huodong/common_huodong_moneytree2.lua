--取得可以返金卡的份数
function x310942_Getcan(varMap, varPlayer, extid )
	local time = GetCurrentTime()
	if extid == 2 then		
			if 	time >= x310942_var_TakeOneWeektime then	--2012.5.4 00:00
				if 	time < x310942_var_TakeTwoWeektime then  --2012.5.11 00:00
					return x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[2][1])
				elseif 	time < x310942_var_TakeOneMonthtime then --2012.5.18 00:00
					return x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[2][1])+x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[2][2])
				end	
			return x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[2][1])+x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[2][2])+x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[2][3])
			end
	elseif extid == 3 then			
			if 	time > x310942_var_TakeTwoWeektime then  --2012.5.11 00:00
				if 	time < x310942_var_FirstTwoMonthtime then 	--2012.5.18 00:00
					return x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[3][1])
				elseif time < x310942_var_SceTwoMonthtime then --2012.5.25 00:00
					return x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[3][1])+x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[3][2])
				end	
				return x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[3][1])+x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[3][2])+x310942_GetMD(varMap, varPlayer, x310942_var_WeekMDlist[3][3])
			end	
	elseif extid == 4 then		
			if time > x310942_var_TakeOneMonthtime then --2012.6.1 00:00
				return x310942_GetMD(varMap, varPlayer, x310942_var_CountMDlist[4])
			end
	elseif extid == 5 then			
			if time > x310942_var_TakeTwoMonthtime then --2012.7.1 00:00	
			return	x310942_GetMD(varMap, varPlayer, x310942_var_CountMDlist[5])		
			end	
	end
	
	return 0		
end

