function x310604_ValidateRelation(varMap, varPlayer, varTalknpc)

	if (x310604_var_PrintDebugLog == 1) then
		WriteLog(1, format("x310604_ValidateRelation --- varMap=%d varPlayer=%d varTalknpc=%d, awardType=%d",
				varMap, varPlayer, varTalknpc, x310604_var_TempPlayerRelationTable[varPlayer]))
	end
	
	local varNpcSnid = GetMonsterGUID(varMap, varTalknpc)
	if (x310604_var_TempPlayerRelationTable[varPlayer] == 1) then -- БОШЫ
		if (x310604_IsSamePlayer(varMap, varPlayer, varNpcSnid) == 0) then
			x310604_SendClientMsg(varMap, varPlayer, varTalknpc, "ФњВЛЪЧЙкОќ!!!")
			return 0
		end
	end
	
	if (x310604_var_TempPlayerRelationTable[varPlayer] == 2) then -- БОЙњМв
		if (x310604_IsSameCountry(varMap, varPlayer, varNpcSnid) == 0) then
			x310604_SendClientMsg(varMap, varPlayer, varTalknpc, "ФњИњЙкОќВЛЪЧЭЌвЛЙњМв!!!")
			return 0
		end
	end
	
	if (x310604_var_TempPlayerRelationTable[varPlayer] == 3) then -- БОАяЛс
		if (x310604_IsSameGuild(varMap, varPlayer, varNpcSnid) == 0) then
			x310604_SendClientMsg(varMap, varPlayer, varTalknpc, "ФњИњЙкОќВЛЪЧЭЌвЛАяЛс!!!")
			return 0
		end
	end
	
	if (x310604_var_TempPlayerRelationTable[varPlayer] == 4) then -- КУгб
		if (x310604_IsFriends(varMap, varPlayer, varNpcSnid) == 0) then
			x310604_SendClientMsg(varMap, varPlayer, varTalknpc, "ФњВЛЪЧЙкОќЕФКУгб!!!")
			return 0
		end
	end
	
	return 1
end

