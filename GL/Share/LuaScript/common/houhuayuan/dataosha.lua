function x920010_ProcPlayerDie(varMap, varPlayer, varKiller)
	
	local nKillerType = GetObjType(varMap, varKiller)
	
	--輝姫繁宀葎講麗夸卦指
	if nKillerType == 2 then
		return
	end
	
	if nKillerType == 3 then
		varKiller = GetOwnerID(varMap, varKiller)
	end	
	
	local minOfDay = GetMinOfDay()
	local weekIdx = GetWeek()	
	local teamid = GetTeamId(varMap, varKiller)
	local teamnum = GetNearTeamCount(varMap, varKiller)
	local GetWeekcount = mod (GetWeekIndex() , 2)
	
	if GetWeekcount == 0 then
		return
	end	
	
	
	if varMap ~= 44 then
		return
	end
	
	if minOfDay < 869 or minOfDay > 900 then
		return
	end

	if weekIdx ~= 6 then
		return
	end

	if x920010_GetSelfBuffid(varMap, varKiller) == 0 then
		SendSpecificImpactToUnit(varMap, varKiller, varKiller, varKiller, 30549, 0)
		return
	end
	if teamid == -1 then
		local Level = GetLevel(varMap, varKiller)
		local n = x920010_AddExpParameterToPlayer(varMap, varKiller)
		local nExp = Level * x920010_var_AddExpParameter[n].killer
		AddExp(varMap, varKiller, nExp)
		Msg2Player(varMap, varKiller,format("似姫黍繁頼撹▷資誼%u泣将刮", nExp),8, 2)
		WriteLog(1, format("x920010_ProcPlayerDie: varMap = %d, PlayerGUID = %u, AddExp = %d", varMap,  GetGUID(varMap, varKiller),nExp))
	else
		for varI = 0, teamnum - 1 do
			local memberId = GetNearTeamMember(varMap, varKiller, varI)
			local Level = GetLevel(varMap, memberId)
			local n = x920010_AddExpParameterToPlayer(varMap, memberId)
			local nExp = Level * x920010_var_AddExpParameter[n].killer			
			if GetHp(varMap, memberId) > 0 then
				AddExp(varMap, memberId, nExp)
				Msg2Player(varMap, memberId,format("似姫黍繁頼撹▷資誼%d泣将刮", nExp),8, 2)
				WriteLog(1, format("x920010_ProcPlayerDieteam: varMap = %d, PlayerGUID = %u, AddExp = %d", varMap, GetGUID(varMap, memberId),nExp))
			end
		end			
	end			
	if x920010_GetSelfBuffid(varMap, varKiller) ~= 0 and nKillerType ~= 2 then
		if x920010_GetSelfBuffid(varMap, varKiller) == 1 then
			x920010_var_buffnum[GetGUID(varMap, varKiller)] = x920010_var_PlayerBuff[x920010_GetSelfBuffid(varMap, varKiller)].buffid
			return
		else		
			x920010_var_buffnum[GetGUID(varMap, varKiller)] = x920010_var_PlayerBuff[x920010_GetSelfBuffid(varMap, varKiller) - 1].buffid
			DispelSpecificImpact(varMap, varKiller, x920010_var_PlayerBuff[x920010_GetSelfBuffid(varMap, varKiller)].buffid, 1)
			SendSpecificImpactToUnit(varMap, varKiller, varKiller, varKiller, x920010_var_buffnum[GetGUID(varMap, varKiller)], 0)			
		end
	end
end



