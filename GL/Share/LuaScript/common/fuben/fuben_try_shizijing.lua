					SetFubenData_Param(varMap, x700210_CSP_BOSS1_TIME_COUNT,0)
					
				end
			end
		end

		
	elseif nBoss1Status==2 then
		if nBoss1TimeCount==x700210_var_Boss_Relive_Time then
			x700210_CreateBoss(varMap, 0)
			
		end
	end

	if nBoss2Status==2 then
		if nBoss2TimeCount==x700210_var_Boss_Relive_Time then
			x700210_CreateBoss(varMap, 1)
		end
	end
	
