function x700227_CreateBoss(varMap)


	local copyscenelevel = GetFubenData_Param(varMap, x700227_CSP_FUBENLEVEL ) ;

	
	for varI, item in x700227_var_BossCreate do

        if item.levelmin <= copyscenelevel and item.levelmax >= copyscenelevel then
		    local varRet = 0
			if item.title ~="" then
				varRet = CreateMonster(varMap, item.type, item.varX, item.z, item.ai, item.aiscript, x700227_var_FileId, -1, 21,-1, item.facedir,  "",item.title)
			else
				varRet = CreateMonster(varMap, item.type, item.varX, item.z, item.ai, item.aiscript, x700227_var_FileId, -1, 21,-1, item.facedir)
			
			end
			
			SetFubenData_Param(varMap, x700227_CSP_BOSS1_ID, varRet )
			break;
				
        end
	end

end

function x700227_CreateRandomBoss(varMap)


	local rate = random(0,100)/100;
	if rate>= x700227_var_RandomBossRate then
		return 0;
	end

	local nFlag = random(0,0)

	

	local copyscenelevel = GetFubenData_Param(varMap, x700227_CSP_FUBENLEVEL ) ;


	local nLen =0;
	for varI, item in x700227_var_Boss_Random_Pos do
		nLen = nLen+1
	end

	local n = random(1,nLen)
	
	local varX=x700227_var_Boss_Random_Pos[n].varX;
	local z=x700227_var_Boss_Random_Pos[n].z;
	
	for varI, item in x700227_var_Boss_Random_Create do  
		if item.varFlag == nFlag then
			if item.levelmin <= copyscenelevel and item.levelmax >= copyscenelevel then
				if item.title ~="" then
					CreateMonster(varMap, item.type, varX,z, item.ai, item.aiscript, -1, -1, 21,-1,item.facedir,  "",item.title)
				else
					CreateMonster(varMap, item.type, varX,z, item.ai, item.aiscript, -1, -1, 21,-1,item.facedir)
				
				end
			end
		end
	end

	

end








function x700227_CreateMonster(varMap)


    local copyscenelevel = GetFubenData_Param(varMap, x700227_CSP_FUBENLEVEL ) ;

	for varI, item in x700227_var_MonsterCreate do  

        if item.levelmin <= copyscenelevel and item.levelmax >= copyscenelevel then
            for j=0,item.varCount -1 do

                local rx = random(-item.r,item.r)
                local varX = item.varX + rx
                local rz = random(-item.r,item.r)
                local z = item.z + rz
				if item.title ~="" then
					CreateMonster(varMap, item.type, varX, z, item.ai, item.aiscript, -1, -1, 21,-1,item.facedir, "", item.title)
				else
					CreateMonster(varMap, item.type, varX, z, item.ai, item.aiscript, -1, -1, 21,-1,item.facedir)
				
				end
			end
        end
	end


	

end

function x700227_CreateRandomMonster(varMap)


	local rate = random(0,100)/100;
	if rate>= x700227_var_RandomMonsterRate then
		return 0;
	end

	local nFlag = random(0,3)

	

	local copyscenelevel = GetFubenData_Param(varMap, x700227_CSP_FUBENLEVEL ) ;


	
	
	for varI, item in x700227_var_Monster_Random_Create do 
		if item.varFlag == nFlag then
			if item.levelmin <= copyscenelevel and item.levelmax >= copyscenelevel then
				local nBossID =0;
				if item.title~="" then
					nBossID = CreateMonster(varMap, item.type, item.varX,item.z, item.ai, item.aiscript, x700227_var_FileId, -1, 21,-1,item.facedir,  "",item.title)
				else
					nBossID = CreateMonster(varMap, item.type, item.varX,item.z, item.ai, item.aiscript, x700227_var_FileId, -1, 21,-1,item.facedir)
				
				end

				SetFubenData_Param(varMap, x700227_B_CREATE_RANDOMBOSS, 1)
				SetFubenData_Param(varMap, x700227_RANDOMBOSS_FLAG, nFlag)

				SetFubenData_Param(varMap, x700227_CSP_RANDOM_BOSS_ID, nBossID)


			end
		end
	end

	x700227_ShowTipsToAll(varMap, "战神再现！")

end



