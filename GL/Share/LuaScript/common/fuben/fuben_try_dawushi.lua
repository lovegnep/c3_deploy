function x700206_CreateBoss(varMap)


	local copyscenelevel = GetFubenData_Param(varMap, x700206_CSP_FUBENLEVEL ) ;
	SetFubenData_Param(varMap, x700206_CSP_BOSS_COUNT, 0 )


	for varI, item in x700206_var_BossCreate do  

        if item.levelmin <= copyscenelevel and item.levelmax >= copyscenelevel then
		    local varRet = 0;
			if item.title~="" then
				varRet = CreateMonster(varMap, item.type, item.varX, item.z, item.ai, item.aiscript, x700206_var_FileId, -1, 21,-1,item.facedir,"",item.title)
			else
				varRet = CreateMonster(varMap, item.type, item.varX, item.z, item.ai, item.aiscript, x700206_var_FileId, -1, 21,-1,item.facedir)
			end

			if item.varFlag == 2 then
				SetFubenData_Param(varMap, x700206_CSP_SPECIALMONSTER1, varRet )
			end

			if item.varFlag==0 then
				SetFubenData_Param(varMap, x700206_CSP_BOSS1, varRet )
			elseif item.varFlag==1 then
				SetFubenData_Param(varMap, x700206_CSP_BOSS2, varRet )
			elseif item.varFlag==2 then
				SetFubenData_Param(varMap, x700206_CSP_BOSS3, varRet )
			end
        end
	end

end




